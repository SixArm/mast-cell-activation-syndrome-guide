<h1 id="water-triggers">Water triggers</h1>
<p>Water is crucial for life. But for some mast cell patients, water can be a surprising source of triggers. For these patients, it can help to use <a href="/water-filters/">water filters</a>.</p>
<p><strong>Key points</strong>:</p>
<ul>
<li><p>Water may contain elements and minerals and chemicals, such as calcium (Ca), sodium (Na), postassium (K), magnesium (Mg), chlorine (Cl), silicon (Si), iron (Fe), manganese (Mn), sulfur (S), hydrogencarbonate (HCO), sulfate (SO4), ammonia, chlorides, chloramines, nitrites, nitrates, various salts, and more.</p></li>
<li><p>Water may contain treatment chemicals, such as chlorine/chlorides or fluorine/flourides. These are chemical additives that government agencies may add, to try to keep the water free of bacteria and to try to keep childrenâ€™s teeth healthy.</p></li>
<li><p>Water treatment plants may add extra chorine sometimes, such as weekends, or times of higher demand, or after long wet periods.</p></li>
<li><p>Water can have various pH levels, ranging from low pH (known as acidic) to high pH (alkaline).</p></li>
<li><p>Water stored in plastic may absorb chemicals, especially if the plastic gets warm, or holds the water for a long time.</p></li>
<li><p>Water that is not clean may contain viruses, bacteria, fungi, toxins, industrial chemicals, environmental runoff, diluted medication from other people using the same water, and more.</p></li>
</ul>
<p>If you react to water, then try these steps. Prefer pure distilled water, not tap water. Prefer water storage with glass, not plastic. Prefer alkaline water, not acidic water. Prefer short showers, not long baths.</p>

