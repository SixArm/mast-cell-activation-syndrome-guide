<ul>
<li><p>Breads, bagels, and rolls, all without salted tops</p></li>
<li><p>Muffins</p></li>
<li><p>Most ready-to-eat cereals</p></li>
<li><p>All rice and pasta, but do not to add salt when cooking</p></li>
<li><p>Low-sodium corn tortillas, flour tortillas</p></li>
<li><p>Low-sodium noodles</p></li>
<li><p>Low-sodium crackers and breadsticks</p></li>
<li><p>Unsalted popcorn, chips, pretzels</p></li>
</ul>
<p>Soups:</p>
<ul>
<li><p>Low-sodium canned and dehydrated soups, broth, bouillon</p></li>
<li><p>Homemade soups without added salt</p></li>
</ul>
<p>Condiments:</p>
<ul>
<li><p>Vinegar</p></li>
<li><p>Unsalted butter or margarine</p></li>
<li><p>Vegetable oils and low sodium sauces and salad dressings</p></li>
<li><p>Mayonnaise</p></li>
<li><p>All desserts made without salt</p></li>
</ul>

