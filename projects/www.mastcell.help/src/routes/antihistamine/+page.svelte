<h1 id="antihistamine">Antihistamine</h1>
<p>Antihistamines are a class of medications that are commonly used to
treat allergic reactions and allergic symptoms. They work by blocking
the effects of histamine, a natural substance in the body that is
released during allergic reactions, that causes symptoms such as
itching, sneezing, runny nose, and swelling.</p>
<p>There are four main types of histamine receptors (H1, H2, H3, and
H4), but antihistamines primarily target the H1 receptors. H1 receptors
are located on various cells, including those in the respiratory and
immune systems.</p>
<p>Common Uses:</p>
<ul>
<li><p>Allergic Rhinitis: Antihistamines are often used to relieve
symptoms of seasonal and perennial allergies, commonly known as hay
fever or allergic rhinitis.</p></li>
<li><p>Allergic Conjunctivitis: They can help alleviate itching and
redness of the eyes associated with allergies.</p></li>
<li><p>Urticaria (Hives): Antihistamines are effective in reducing the
itching and swelling of hives.</p></li>
<li><p>Itching: They can be used to relieve itching associated with
various allergic skin conditions, insect bites, and contact
dermatitis.</p></li>
</ul>

