<h1 id="start-here-for-antihistamines">Start here for antihistamines</h1>
<p>Ask your provider about trying over-the-counter antihistamines, such as ones below. Try a variety to discover which work best for you.</p>
<p>Antihistamine second-generation H1-blockers help with skin symptoms, such as hives or rashes.</p>
<ol type="1">
<li><p><a href="/cetirizine/">Cetirizine</a>, brand name Zyrtec. Try 10mg 2 times per day. If it helps, try increasing to 20mg 2 times per day. If it doesn’t help, then discontinue, and try the next step.</p></li>
<li><p><a href="/fexofenadine/">Fexofenadine</a>, brand name Allegra. Try 180mg 2 times per day. If it helps, try increasing to 360mg 2 times per day. If it doesn’t help, then discontinue, and try the next step.</p></li>
<li><p><a href="/loratadine/">Loratadine</a>, brand name Claritin. Try 10mg 3 times per day. If it helps, try increasing to 10mg 4 times per a day. If it doesn’t help, then discontinue, and try the next step.</p></li>
<li><p><a href="/levocetirizine/">Levocetirizine</a>, brand name Xyzal. Try 5mg 2 times per day. If it helps you, then try increasing to 10mg 2 times per day. If it doesn’t help, then discontinue, and ask your doctor for help.</p></li>
</ol>
<p>Antihistamine second-generation H2-blockers help with gastrointestinal symptoms, such as acid reflux or indigestion.</p>
<ol type="1">
<li><p><a href="/famotidine/">Famotidine</a>, brand name Pepcid. Try 20mg 2 times per day. If it helps, try increasing to 40mg 2 times per day. If it doesn’t help, then discontinue, and try the next step.</p></li>
<li><p><a href="/cimetidine/">Cimetidine</a>, brand name Tagamet. Try 200mg 2 times per day. If it helps, try increasing to 400mg 2 times per day. If it doesn’t help, then discontinue, and try the next step.</p></li>
<li><p>Ask your doctor about other medications, including <a href="/proton-pump-inhibitors/">proton pump inhibitors (PPIs)</a>: esomeprazole (brand name Nexium), lansoprazole (brand name Prevacid), omeprazole (brand name Prilosec).</p></li>
</ol>
<p>Antihistamine first-generation for flares or sleep:</p>
<ol type="1">
<li><a href="/diphenhydramine/">Diphenhydramine</a>, brand name Benadryl. Use as needed for flares, or to help with sleep, in addition to the antihistamines above. Caution: this medication has side effects that may include sedation, confusion, dizziness, etc. Try 25mg once. If it helps, try increasing to 50mg once. Can be taken 25-50mg every 5 hours. If it doesn’t help, then discontinue.</li>
</ol>

