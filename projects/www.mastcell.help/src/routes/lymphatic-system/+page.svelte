<h1 id="lymphatic-system">Lymphatic system</h1>
<p>The lymphatic system is a network of tissues, organs, and vessels that play a vital role in maintaining fluid balance, filtering harmful substances, and supporting the bodyâ€™s immune function. It complements the circulatory system (blood vessels) and is responsible for the circulation of lymph, a clear, colorless fluid that contains white blood cells, known as lymphocytes, and other immune cells.</p>
<p><strong>Key functions</strong>:</p>
<ul>
<li><p><strong>Fluid Balance</strong>: Collect excess interstitial fluid (the fluid that surrounds cells) and return it to the bloodstream. This prevents the accumulation of excess tissue fluid.</p></li>
<li><p><strong>Immune Response</strong>: Filter lymph and trap foreign particles, such as bacteria, viruses, and abnormal cells. Recognize and attack foreign invaders.</p></li>
<li><p><strong>Lymphatic Vessels</strong>: Lymphatic vessels, also called lymphatics, are a network of thin-walled vessels that transport lymph throughout the body. Lymphatic capillaries, which are even smaller than blood capillaries, collect excess tissue fluid and transport it to larger lymphatic vessels.</p></li>
<li><p><strong>Lymphatic Organs</strong>: The lymphatic system includes various lymphatic organs, with the primary ones being lymph nodes, the spleen, the thymus, and the tonsils. These organs house immune cells and play critical roles in filtering and processing lymph.</p></li>
<li><p><strong>Bone Marrow</strong>: While not traditionally considered a part of the lymphatic system, the bone marrow is essential for the production of white blood cells, including lymphocytes, which circulate through the lymphatic system.</p></li>
</ul>

