<h1 id="polycystic-ovary-syndrome-pcos">Polycystic Ovary Syndrome (PCOS)</h1>
<p>Polycystic Ovary Syndrome (PCOS) is a common hormonal disorder that affects people with ovaries, particularly during their reproductive years. PCOS is a chronic condition that requires ongoing management.</p>
<p><strong>Key points</strong>:</p>
<ul>
<li><p><strong>Irregular Menstrual Cycles</strong>: Symptoms can manifest as infrequent periods, prolonged periods, or unpredictable menstrual cycles.</p></li>
<li><p><strong>Hyperandrogenism</strong>: PCOS is often associated with higher levels of androgens, which are male hormones that females also produce. This may lead to symptoms such as hirsutism (excessive hair growth), acne, and male-pattern baldness.</p></li>
<li><p><strong>Ovulatory Dysfunction</strong>: Individuals with PCOS may experience anovulation, where ovaries do not release eggs regularly.</p></li>
<li><p><strong>Polycystic Ovaries</strong>: The term “polycystic” refers to the appearance of follicles on the ovaries on ultrasound.</p></li>
<li><p><strong>Insulin Resistance</strong>: Insulin resistance is when the body’s cells don’t respond effectively to insulin. This is common in PCOS.</p></li>
<li><p><strong>Metabolic Abnormalities</strong>: PCOS is associated with an increased risk of metabolic issues, including type 2 diabetes, high blood pressure, and abnormal cholesterol levels.</p></li>
<li><p><strong>Treatment</strong>: May include hormonal contraceptives (birth control pills) to regulate menstrual cycles, anti-androgen medications to manage symptoms like hirsutism, and lifestyle changes for weight management, diet, and exercise.</p></li>
<li><p><strong>Fertility Concerns</strong>: Fertility treatments such as ovulation induction or assisted reproductive technologies like in vitro fertilization (IVF) may be considered.</p></li>
</ul>

