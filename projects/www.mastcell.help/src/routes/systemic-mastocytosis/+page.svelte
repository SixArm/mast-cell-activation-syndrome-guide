<h1 id="systemic-mastocytosis-sm">Systemic mastocytosis (SM)</h1>
<p>Systemic mastocytosis (SM) affects the skin and also other organs and
tissues in the body.</p>
<p>There are several subtypes of systemic mastocytosis, including:</p>
<ul>
<li><p>Indolent Systemic Mastocytosis: This is the most common subtype
of systemic mastocytosis and is usually not associated with aggressive
symptoms or organ damage.</p></li>
<li><p>Systemic Mastocytosis with an Associated Hematologic Neoplasm
(SM-AHN): This subtype is characterized by the presence of a second
blood disorder, such as myelodysplastic syndrome or myeloproliferative
neoplasms.</p></li>
<li><p>Aggressive Systemic Mastocytosis (ASM): This subtype is more
severe and may involve organ damage or failure.</p></li>
<li><p>Mast Cell Leukemia (MCL): This is the rarest and most aggressive
form of systemic mastocytosis, where mast cells accumulate in the bone
marrow and peripheral blood.</p></li>
</ul>

