<h1 id="environmental-triggers">Environmental triggers</h1>
<p>Environmental triggers refer to adverse reactions that individuals may experience in response to exposure to various environmental factors. These sensitivities cause symptoms such as sneezing, nasal congestion, itching, and respiratory issues, and can cause severe symptoms including <a href="/anaphylaxis/">anaphylaxis</a>.</p>
<p><strong>Examples</strong>:</p>
<ul>
<li><p><strong>Air triggers</strong>: Examples are plant pollens, mold spores, dust, mites, pet dander, pollution, smoke, excess carbon monoxide, and volatile organic compounds (VOCs) from household products or cooking.</p></li>
<li><p><strong>Water triggers</strong>: Examples are dissolved chlorine or bleach, certain cleaners and detergents, certain water chemicals, and water-borne bacteria, germs, viruses, parasites, and algae. Water can also have triggers due to osmolarity, hardness, acid/base pH, and hot/cold temperatures.</p></li>
<li><p><strong>Building triggers</strong>: Examples are carpet outgasses, industrial cleaning agents, furniture fabric treatments, paint fumes, and sick building syndrome.</p></li>
<li><p><strong>Temperature triggers</strong>: Examples are extreme temperatures, whether hot or cold, such as a hot shower or cold bath, or being outside in hot or cold weather.</p></li>
<li><p><strong>Electromagnetic Hypersensitivity (EHS)</strong>: EHS is characterized by symptoms that individuals attribute to exposure to electromagnetic fields (EMFs) from sources like electronic devices, Wi-Fi, and power lines.</p></li>
</ul>

