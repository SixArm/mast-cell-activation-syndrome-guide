<h1 id="mast-cell-tryptase-stimulation-testing">Mast cell tryptase stimulation testing</h1>
<p>Mast cell tryptase stimulation testing, also known as the Tryptase Release Test (TRT), is a diagnostic test used to assess mast cell activation. This test measures the levels of tryptase, an enzyme released by mast cells, before and after a provoking stimulus that triggers mast cell degranulation.</p>
<p><strong>Overview:</strong></p>
<ul>
<li><p><strong>Purpose:</strong> The primary purpose of this test is to assess mast cell responsiveness to certain stimuli. It can be particularly useful in cases where baseline tryptase levels are within the normal range, but there is still suspicion of mast cell activation syndrome (MCAS) or other mast cell disorders.</p></li>
<li><p><strong>Procedure:</strong> Before the test, a baseline blood sample is taken to measure the initial levels of tryptase. The patient is then exposed to a provoking stimulus that triggers mast cell degranulation. This stimulus can vary and may include physical exercise, certain medications, or other triggers known to provoke mast cell release. After the stimulus, additional blood samples are taken.</p></li>
<li><p><strong>Interpretation:</strong> An increase in tryptase levels after the provoking stimulus suggests mast cell activation in response to that specific trigger.</p></li>
</ul>

