<h1 id="rheumatoid-arthritis-ra">Rheumatoid arthritis (RA)</h1>
<p>Rheumatoid arthritis (RA) is an autoimmune disease that primarily affects the joints but can also impact other parts of the body. It is characterized by inflammation in the synovium, the lining of the membranes that surround the joints. This chronic inflammation can lead to joint damage, pain, stiffness, and functional impairment.</p>
<p><strong>Key points</strong>:</p>
<ul>
<li><p><strong>Symptoms</strong>: Common symptoms of RA include joint pain, swelling, and stiffness, which are often more pronounced in the morning or after periods of inactivity.</p></li>
<li><p><strong>Diagnosis</strong>: Blood tests look for rheumatoid factor and anti-cyclic citrullinated peptide or anti-CCP antibodies. Imaging studies can help.</p></li>
<li><p><strong>Treatment</strong>: Treatment for RA aims to reduce inflammation, relieve pain, slow down joint damage, and improve the patient’s quality of life. Common treatments include disease-modifying antirheumatic drugs (DMARDs), nonsteroidal anti-inflammatory drugs (NSAIDs), and biologic therapies. Lifestyle modifications include physical therapy, stopping smoking, stress management, and exercise.</p></li>
<li><p><strong>Autoimmune Disease</strong>: In RA, the body’s immune system mistakenly attacks healthy tissues, primarily the synovium, causing inflammation.</p></li>
<li><p><strong>Progressive Joint Damage</strong>: Over time, persistent inflammation can lead to joint damage and deformities. This joint damage can be irreversible.</p></li>
<li><p><strong>Extra-Articular Manifestations</strong>: RA can affect organs and systems outside the joints, leading to complications such as inflammation of the heart lining (pericarditis), lung disease, and an increased risk of cardiovascular disease.</p></li>
</ul>

