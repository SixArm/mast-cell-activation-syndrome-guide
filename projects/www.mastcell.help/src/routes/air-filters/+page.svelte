<h1 id="air-filters">Air filters</h1>
<p>Air filters are devices designed to remove particles, allergens, contaminants, and pollutants from the air, improving indoor air quality. These filters are commonly used in air purifiers and HVAC (Heating, Ventilation, and Air Conditioning) systems,</p>
<p>When choosing an air filter for an air purifier, consider the specific pollutants you want to target, the size of particles to be filtered, and the clean air delivery rate (CADR) of the purifier. See also <a href="/activated-carbon-filters/">activated carbon filters</a>.</p>
<p><strong>Common types</strong>:</p>
<ul>
<li><p><strong>HEPA Filters (High-Efficiency Particulate Air)</strong>: HEPA filters are highly efficient in capturing particles as small as 0.3 microns. They are effective for trapping dust, pollen, mold spores, pet dander, and other airborne particles.</p></li>
<li><p><strong>MERV-Rated Filters</strong>: MERV (Minimum Efficiency Reporting Value) ratings indicate the effectiveness of air filters. MERV-rated filters are commonly used in HVAC systems, with higher ratings indicating better filtration.</p></li>
<li><p><strong>Activated Carbon Filters</strong>: These excel in removing gases, odors, and volatile organic compounds (VOCs) from the air. They are often used in air purifiers and HVAC systems.</p></li>
<li><p><strong>Electrostatic Filters</strong>: Electrostatic filters use an electric charge to attract and capture particles. They may be washable or disposable and are commonly used in HVAC systems.</p></li>
<li><p><strong>UV-C Air Purifiers</strong>: UV-C air purifiers use ultraviolet (UV) light to neutralize or kill microorganisms, such as bacteria and viruses. They are often used in conjunction with other filters.</p></li>
</ul>

