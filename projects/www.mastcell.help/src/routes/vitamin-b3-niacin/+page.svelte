<h1 id="vitamin-b3-niacin">Vitamin B3 (niacin)</h1>
<p>Vitamin B3 (also known as niacin or nicotinic acid) is one of the water-soluble B-vitamins. Niacin is involved in several important functions, including energy production, DNA repair, and the maintenance of healthy skin and nervous system.</p>
<p><strong>Key points</strong>:</p>
<ul>
<li><p><strong>Energy Metabolism</strong>: Niacin is a key component of the coenzymes NAD (nicotinamide adenine dinucleotide) and NADP (nicotinamide adenine dinucleotide phosphate). These coenzymes play a vital role in converting carbohydrates, fats, and proteins into energy.</p></li>
<li><p><strong>DNA Repair</strong>: Niacin is important for DNA repair and synthesis, which is essential for the proper functioning and replication of cells.</p></li>
<li><p><strong>Skin Health</strong>: Adequate niacin intake is necessary for maintaining healthy skin. It can help improve skin conditions like acne and reduce the risk of certain skin disorders.</p></li>
<li><p><strong>Cardiovascular Health</strong>: Niacin can raise high-density lipoprotein (HDL) cholesterol (often called “good” cholesterol) and lower low-density lipoprotein (LDL) cholesterol (often called “bad” cholesterol).</p></li>
<li><p><strong>Nervous System Function</strong>: Niacin is essential for the proper functioning of the nervous system. Deficiency can lead to a condition known as pellagra, which is characterized by neurological symptoms.</p></li>
<li><p><strong>Deficiency</strong>: Severe niacin deficiency can lead to pellagra, which presents with symptoms like diarrhea, dermatitis, and dementia.</p></li>
</ul>
<p>Dietary sources include meat, poultry, fish, dairy products, eggs, nuts, and legumes.</p>

