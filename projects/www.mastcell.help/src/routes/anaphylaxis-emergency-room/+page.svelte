<h1 id="anaphylaxis-emergency-room">Anaphylaxis emergency room</h1>
<p><a href="/anaphylaxis/">Anaphylaxis</a> emergency hospital treatment in an emergency room, or emergency department, or by an emergency physician:</p>
<ol type="1">
<li><p>Administer adrenaline (a.k.a. epinephrine) if patient suspects anaphylaxis. Prefer preservative-free adrenaline. Administer adrenaline intramuscularly (IM) (Vasctus Lateralis muscle). Concentration is 1:10000 (1mg/1ml) solution - 0.3mg. May repeat in 15 minutes if needed. Because of the risk of potentially lethal arrhythmias, adrenaline should be administered IV only in profoundly hypotensive patients or patients in cardio/respiratory arrest who have failed to respond to IV volume replacement and several injected doses of epinephrine.</p></li>
<li><p>Administer oxygen.</p></li>
<li><p>Start large bore IV access.</p></li>
<li><p>If hypotensive, then administer IV fluids.</p></li>
<li><p>If wheezing is present, then consider inhaled or nebulized bronchodilators
Search instead for bronchodilators (e.g. albuterol).</p></li>
<li><p>Administer H1 and H2 blockers as supportive therapy. Prefer IV administration. Include <a href="/diphenhydramine/">diphenhydramine</a> as an H1 blocker, given 25mg-50mg (or hydroxyzine/fexofenadine/), and 1mg/kg; given very slow IV diluted in normal saline over 2-5 minutes. H2 antagonist, ranitidine/cimetidine, IV, should also be given.</p></li>
<li><p>Administer <a href="/corticosteroids/">corticosteroids</a> (e.g. prednisone/) because they may prevent prolonged anaphylaxis.</p></li>
</ol>

