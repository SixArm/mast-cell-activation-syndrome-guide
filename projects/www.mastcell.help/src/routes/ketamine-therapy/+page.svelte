<h1 id="ketamine-therapy">Ketamine therapy</h1>
<p>Ketamine is an anesthetic medication that has therapy use in mental health treatments, such as for major depressive disorder (MDD), bipolar disorder, and treatment-resistant depression (TRD).</p>
<p><strong>Key points</strong>:</p>
<ul>
<li><p><strong>NMDA Receptor Modulation</strong>: Ketamine acts as a non-competitive antagonist of the N-methyl-D-aspartate (NMDA) receptor, leading to increased levels of the neurotransmitter glutamate. This, in turn, triggers a cascade of effects that may contribute to its antidepressant properties.</p></li>
<li><p><strong>Rapid Onset of Effects</strong>: Individuals may experience improvements in mood within hours or days, in contrast to the delayed onset often seen with traditional antidepressants.</p></li>
<li><p><strong>Administration Routes</strong>: Ketamine can be administered via different routes, including intravenous (IV) infusion, intramuscular injection, or, more recently, as a nasal spray called esketamine.</p></li>
<li><p><strong>Treatment-Resistant Depression</strong>: Ketamine is often considered in cases of treatment-resistant depression where individuals have not responded to other standard antidepressant treatments.</p></li>
