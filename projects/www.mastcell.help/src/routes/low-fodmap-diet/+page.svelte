<h1 id="low-fodmap-diet">Low-FODMAP diet</h1>
<p>A low-FODMAP diet is a dietary approach designed to manage and alleviate gastrointestinal symptoms, particularly in individuals with irritable bowel syndrome (IBS) and certain other digestive conditions. The goal is to identify and manage specific triggers, then develop an eating plan that minimizes discomfort while providing nutrition and variety.</p>
<p>FODMAP is an acronym that stands for:</p>
<pre><code>* Fermentable Oligosaccharides: Found in foods like wheat, rye, onions, garlic, and legumes.

* Disaccharides: Lactose-containing products such as milk, yogurt, and some soft cheeses.

* Monosaccharides: Fruits like apples, pears, and honey that contain excess fructose.

* Polyols: Found in certain fruits (e.g., cherries, peaches) and sugar alcohols (e.g., sorbitol, mannitol).</code></pre>
<p>FODMAPs are types of carbohydrates that can be poorly absorbed in the small intestine. When these carbohydrates reach the colon, they can be fermented by gut bacteria, leading to gas production and gastrointestinal discomfort in some people.</p>
