<h1 id="adrenaline-auto-injector-instructions">Adrenaline auto-injector instructions</h1>
<p>When to use your adrenaline auto-injector (AAI) such as your brand name EpiPen®?</p>
<p>Use your adrenaline auto-injector immediately if you experience ANY of the following serious symptoms of anaphylaxis, or anaphylactic shock, or anaphylactoid shock:</p>
<ul>
<li><p>Breathing difficulties, such as fast, shallow breathing, or wheezing</p></li>
<li><p>A fast heartbeat</p></li>
<li><p>Clammy skin</p></li>
<li><p>Feeling light-headed or faint</p></li>
<li><p>Confusion and anxiety</p></li>
<li><p>Collapsing</p></li>
<li><p>Loss of consciousness</p></li>
</ul>
<p>If you are only experiencing the following mild symptoms, then take your usual anti-histamines and treatments, and in addition have your adrenaline auto-injector ready in case of worsening symptoms:</p>
<ul>
<li><p>Red, raised, itchy rash</p></li>
<li><p>Swelling of face, lips or eyelids</p></li>
<li><p>Abdominal pain, nausea and/or vomiting</p></li>
<li><p>Tingling mouth</p></li>
</ul>
<p>Source: “EpiPen Explained” by the EpiPen company, edited for clarity.</p>

