<h1 id="diphenhydramine">Diphenhydramine</h1>
<p>Diphenhydramine is an antihistamine that is commonly used to relieve symptoms associated with allergies and allergic reactions.</p>
<p>It is available over-the-counter. In the United States, see brand name Benadryl. Be aware that some Diphenhydramine medications contain artificial colors that may affect individuals, so ask about colorless versions of the medication.</p>
<p><strong>Key points:</strong></p>
<ul>
<li><p><strong>Mechanism of Action:</strong> Diphenhydramine works by blocking the effects of histamine, a natural substance produced by the body during an allergic reaction.</p></li>
<li><p><strong>Indications:</strong> It is used to relieve symptoms of allergies, including itching, rash, watery eyes, sneezing, and runny nose. It is also used to treat motion sickness, insomnia (as a sleep aid), and symptoms of the common cold.</p></li>
<li><p><strong>Forms:</strong> It is available in various forms, including oral tablets, capsules, liquid, and as a topical cream.</p></li>
<li><p><strong>Sedative Effect:</strong> Diphenhydramine has a sedative effect and can cause drowsiness. Because of this, it is often used as a sleep aid.</p></li>
<li><p><strong>Timing:</strong> Effects typically last for 4-6 hours.</p></li>
<li><p><strong>Side Effects:</strong> Common side effects may include drowsiness, dry mouth, blurred vision, constipation, or urinary retention. It may also cause excitability, especially in children.</p></li>
<li><p><strong>Interactions:</strong> Diphenhydramine may interact with other medications, including central nervous system depressants.</p></li>
</ul>

