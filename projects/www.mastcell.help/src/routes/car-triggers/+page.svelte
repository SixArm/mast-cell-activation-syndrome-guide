<h1 id="car-triggers">Car triggers</h1>
<p>Cars can have many triggers. If you drive, then consider this list.</p>
<p><strong>Examples</strong>:</p>
<ul>
<li><p><strong>Airborne</strong>: Examples: volatile organic chemicals from dashboard plastics, off-gas from car seat foam, air fresheners, mold, mildew, fungus, exhaust fumes.</p></li>
<li><p><strong>Contact</strong>: Examples: fabric treatments, leather guard, detailing cleaners, thermoset plastics, dust, dirt, mites.</p></li>
<li><p><strong>Situation-dependent</strong>: Examples: emotional stress from driving, sounds or vibrations, bright lights.</p></li>
</ul>
<p><strong>Diagnostics</strong>:</p>
<ul>
<li><p><strong>Airborne</strong>: Try being in your car, engine off, wearing a painterâ€™s respirator.</p></li>
<li><p><strong>Contact</strong>: Try being in your car, engine off, wearing full clothes, gloves, and goggles.</p></li>
<li><p><strong>Situation-dependent</strong>: Try the above protections, while driving safely.</p></li>
</ul>
<p><strong>Treatments</strong>:</p>
<ul>
<li><p><strong>Cleaning</strong>: Have your car professionally cleaned with all hypoallergenic products. Do a mold test.</p></li>
<li><p><strong>Filtering</strong>: Add an air filters, such as one that plugs into your 12-volt outlet. Change your HVAC air filters.</p></li>
<li><p><strong>Vibrations</strong>: If everything else is ruled out other than vibrations, then ask a car mechanic for help. Consider better car shock absorbers, or disable the stop-start system, or check the engine mounts, or balance the tires and wheels, or tune up the engine, or get a better harmonic balancer, or even experiment with different cars such as going from gasoline to electric.</p></li>
</ul>

