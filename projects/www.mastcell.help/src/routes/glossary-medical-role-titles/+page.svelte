<h1 id="glossary-of-medical-role-titles">Glossary of medical role titles</h1>
<p><strong>Doctor physician titles:</strong></p>
<ul>
<li><p><strong>MD:</strong> Medical Doctor. Focus is allopathic western medicine.</p></li>
<li><p><strong>DO:</strong> Doctor of Osteopathy. Focus is holistic wellness and musculoskeletal areas.</p></li>
<li><p><strong>ND:</strong> Naturopathic Doctor. Focus is natural remedies to help the body heal itself.</p></li>
</ul>
<p><strong>Assistant-related titles:</strong></p>
<ul>
<li><p><strong>PA:</strong> Physician Assistant.</p></li>
<li><p><strong>PA-C:</strong> Physician Assistant, Certified.</p></li>
</ul>
<p><strong>Nurse-related titles:</strong></p>
<ul>
<li><p><strong>RN:</strong> Registered Nurse.</p></li>
<li><p><strong>APRN:</strong> Advanced Practice Registered Nurse.</p></li>
<li><p><strong>APRN-NP:</strong> Nurse Practitioner.</p></li>
<li><p><strong>APRN-FNP:</strong> Family Nurse Practitioner.</p></li>
<li><p><strong>APRN-ANP:</strong> Adult Nurse Practitioner.</p></li>
<li><p><strong>APRN-GNP:</strong> Geriatric Nurse Practitioner.</p></li>
<li><p><strong>APRN-AGACNP:</strong> Adult-Gerontology Acute Care Nurse Practitioner.</p></li>
<li><p><strong>APRN-PNP:</strong> Psychiatric Nurse Practitioner.</p></li>
<li><p><strong>APRN-CNM:</strong> Certified Nurse Midwife.</p></li>
<li><p><strong>ACNP:</strong> Acute Care Nurse Practitioner.</p></li>
<li><p><strong>ENP:</strong> Emergency Nurse Practitioner.</p></li>
</ul>

