<h1 id="low-oxalate-diet">Low-oxalate diet</h1>
<p>A low-oxalate diet is a dietary approach that restricts the intake of foods high in oxalates. Oxalates are natural compounds found in a variety of plant-based foods and are also produced by the human body. In individuals who are prone to kidney stones or have certain medical conditions, a low-oxalate diet may be recommended to reduce the risk of developing kidney stones or to manage related health issues.</p>
<p>Oxalates can bind with calcium in the body to form crystals, which can contribute to the development of kidney stones. Kidney stones are hard mineral deposits that can form in the kidneys and may cause pain and other complications when they block the urinary tract. By reducing dietary oxalate intake, it is believed that the risk of kidney stone formation can be minimized.</p>
<ul>
<li><p><strong>Foods to choose</strong>: low-oxalate vegetables (e.g. carrots, cucumbers, lettuce, zucchini), most fruits, most grains (e.g. rice, wheat).</p></li>
<li><p><strong>Foods to avoid</strong>: high-oxalate vegetables (e.g. spinach, chard, beets, rhubarb), most nuts and seeds (e.g. (almonds, peanuts, sesame), potatoes, yams, chocolate, certain fruits (e.g. blackberries, raspberries, figs), black tea, and some herbal teas.</p></li>
</ul>
<p>It’s important to note that the severity of oxalate-related issues can vary from person to person, and some individuals may need to follow a stricter low-oxalate diet than others.</p>
<p>If you are considering a low-oxalate diet, it’s advisable to work with a healthcare professional or registered dietitian who can provide personalized guidance and ensure that your dietary choices still meet your nutritional needs. They can also monitor your progress and make necessary adjustments to your diet as required.</p>
<h1 id="low-oxalate-diet-foods-to-choose">Low-oxalate diet: foods to choose</h1>
<p>Low-Oxalate Vegetables:</p>
<ul>
<li>Asparagus</li>
<li>Broccoli</li>
<li>Cauliflower</li>
<li>Cabbage</li>
<li>Green beans</li>
<li>Peppers (red, green, yellow)</li>
<li>Zucchini</li>
<li>Lettuce (most types)</li>
<li>Radishes</li>
<li>Snow peas</li>
</ul>
<p>Low-Oxalate Fruits:</p>
<ul>
<li>Apples</li>
<li>Blueberries</li>
<li>Grapes</li>
<li>Pears</li>
<li>Pineapple</li>
<li>Watermelon</li>
<li>Strawberries</li>
<li>Kiwi</li>
</ul>
<p>Protein Sources:</p>
<ul>
<li>Most meats, including chicken, turkey, and pork</li>
<li>Most fresh fish</li>
<li>Eggs</li>
</ul>
<p>Dairy:</p>
<ul>
<li>Low-fat milk</li>
<li>Low-fat yogurt</li>
<li>Low-fat cheese (in moderation)</li>
</ul>
<p>Grains and Starches:</p>
<ul>
<li>White rice</li>
<li>White bread</li>
<li>Pasta (without tomato-based sauces)</li>
<li>Oats</li>
</ul>
<p>Beverages:</p>
<ul>
<li>Water</li>
<li>Herbal teas (without strong oxalate-containing herbs)</li>
<li>Lemonade made with low-oxalate fruits</li>
</ul>
<p>Fats and Oils:</p>
<ul>
<li>Butter</li>
<li>Olive oil</li>
</ul>
<p>Spices and Seasonings:</p>
<ul>
<li>Most herbs and spices (without excessive oxalates)</li>
</ul>
<p>Sweets and Desserts (in moderation):</p>
<ul>
<li>Vanilla ice cream</li>
<li>Sorbet made from low-oxalate fruits</li>
<li>Most cakes and cookies made with low-oxalate ingredients</li>
</ul>
<h1 id="low-oxalate-diet-foods-to-avoid">Low-oxalate diet: foods to avoid</h1>
<p>Vegetables:</p>
<ul>
<li>Spinach</li>
<li>Swiss chard</li>
<li>Beet greens</li>
<li>Collard greens</li>
<li>Kale</li>
<li>Okra</li>
<li>Sweet potatoes</li>
</ul>
<p>Fruits:</p>
<ul>
<li>Rhubarb</li>
<li>Blackberries</li>
<li>Raspberries</li>
<li>Fig</li>
<li>Kiwifruit</li>
<li>Tangerines</li>
<li>Persimmons</li>
</ul>
<p>Nuts and Seeds:</p>
<ul>
<li>Almonds</li>
<li>Peanuts</li>
<li>Cashews</li>
<li>Chia seeds</li>
<li>Pumpkin seeds</li>
<li>Sesame seeds</li>
</ul>
<p>Grains:</p>
<ul>
<li>Whole wheat</li>
<li>Bran cereals</li>
</ul>
<p>Legumes:</p>
<ul>
<li>Beans (all types)</li>
<li>Lentils</li>
</ul>
<p>Beverages:</p>
<ul>
<li>Tea (black tea, green tea, and herbal teas that contain High-oxalate herbs)</li>
<li>Dark beer</li>
<li>Dark colas</li>
</ul>
<p>Dairy Alternatives:</p>
<ul>
<li>Some almond milk and soy milk products (check labels for oxalate content)</li>
</ul>
<p>High-oxalate Spices and Seasonings:</p>
<ul>
<li>Cinnamon</li>
<li>Curry</li>
<li>Nutmeg</li>
<li>Dill</li>
</ul>
<p>Chocolate and Cocoa: These are high in oxalates and should be avoided.</p>
<p>Avoid Excessive Salt: A high-sodium diet can increase the risk of kidney stone formation, so it’s advisable to limit your salt intake.</p>

