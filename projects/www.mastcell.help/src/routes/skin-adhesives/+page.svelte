<h1 id="skin-adhesives">Skin adhesives</h1>
<p>Skin adhesives can cause symptoms, such as when a person uses bandages, or medical monitoring electrodes.</p>
<p><strong>Key points</strong>:</p>
<ul>
<li><p><strong>Ask for help</strong>: Ask yor doctors for extra help because you have mast cell issues and may react to skin adhesives. Ask for hypoallergenic bandages, hypoallergenic electrode paste, and hypoallergenic products.</p></li>
<li><p><strong>Prepare your skin</strong>: Get a small amount of mast cell stabilizer such as cromolyn sodium, or antihistamine such as diphenhydramine, or antiinflammatory such as fluticasone, or sensitive-skin barrier cream, or all of the above. Rub it on your skin where you will be applying the skin adhesive. This can help you reduce symptoms. For example if you have a steroid spray, then spray some on your finger, and rub it on your skin where you will be applying the bandage or electrode.</p></li>
<li><p><strong>Try a different way</strong>: Can you wrap the bandage around your affected area, rather than using skin adhesive? Can you attach the electrode to a tee shirt or athletic bandage that you wrap around your body, rather than using skin adhesive?</p></li>
</ul>

