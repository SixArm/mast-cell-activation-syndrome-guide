<h1 id="low-sodium-diet">Low-sodium diet</h1>
<p>A low-sodium diet, often referred to as a low-salt diet, is a dietary approach that restricts the intake of sodium, a mineral commonly found in salt (sodium chloride). Reducing sodium intake can be beneficial for various reasons, primarily to support overall health and manage specific medical conditions, including hypertension, kidney disease, liver disease, and edema.</p>
<p><strong>Key points</strong>:</p>
<ul>
<li><p><strong>Sodium and Health</strong>: High sodium intake can contribute to high blood pressure (hypertension), which is a significant risk factor for heart disease and stroke.</p></li>
<li><p><strong>Dietary Sources of Sodium</strong>: Common sources include canned soups, fast food, frozen meals, processed meats, snacks, and restaurant dishes. Even foods that don’t taste particularly salty can be high in sodium.</p></li>
<li><p><strong>Recommended Daily Allowance</strong>: The Dietary Guidelines for Americans recommend limiting sodium intake to less than 2300 milligrams (mg) per day for adults, or 150 0mg per day for people with hypertension or other risk factors.</p></li>
<li><p><strong>Low-Sodium Diet Goals</strong>: A low-sodium diet aims to reduce sodium intake by minimizing or eliminating high-sodium foods and incorporating more whole, unprocessed foods into the diet.</p></li>
<li><p><strong>Reading Labels</strong>: To follow a low-sodium diet, it’s essential to read food labels and be aware of sodium content in packaged foods. Look for foods labeled as “low-sodium,” “no-salt-added,” or “sodium-free”. Pay attention to serving sizes, as sodium content is often listed per serving.</p></li>
<li><p><strong>Reducing Salt Use</strong>: Reducing the use of table salt (sodium chloride) in cooking and at the table is an important aspect of a low-sodium diet. Instead of salt, consider using herbs, spices, lemon juice, vinegar, and other seasonings to add flavor to your dishes.</p></li>
<li><p><strong>Eating Out</strong>: When dining out, you can request that your meal be prepared with less salt, and you can avoid adding extra salt. Also, be cautious of hidden sodium in restaurant dishes and fast food.</p></li>
</ul>
<h1 id="low-sodium-diet-foods-to-choose">Low-sodium diet: foods to choose</h1>
<p>Vegetables:</p>
<ul>
<li><p>Fresh and frozen vegetables without sauces</p></li>
<li><p>Low-sodium canned vegetables, sauces, and juices</p></li>
<li><p>Fresh potatoes, frozen French fries and instant mashed potatoes</p></li>
<li><p>Low-salt tomato juice, low-salt V-8 juice</p></li>
</ul>
<p>Fruits:</p>
<ul>
<li><p>Most fresh, frozen, and canned fruit</p></li>
<li><p>Dried fruits</p></li>
</ul>
<p>Proteins:</p>
<ul>
<li><p>Any fresh or frozen beef, lamb, pork, poultry, fish</p></li>
<li><p>Eggs and egg substitutes</p></li>
<li><p>Low-sodium peanut butter</p></li>
<li><p>Dry peas and beans (not canned)</p></li>
<li><p>Low-sodium canned fish</p></li>
<li><p>Drained canned fish or poultry, packed in water or oil</p></li>
</ul>
<p>Dairy:</p>
<ul>
<li><p>Milk, yogurt, ice cream and ice milk</p></li>
<li><p>Low-sodium cheeses, cream cheese, ricotta cheese, mozzarella</p></li>
</ul>
<p>Grains etc…</p>
<ul>
<li><p>Breads, bagels, and rolls, all without salted tops</p></li>
<li><p>Muffins</p></li>
<li><p>Most ready-to-eat cereals</p></li>
<li><p>All rice and pasta, but do not to add salt when cooking</p></li>
<li><p>Low-sodium corn tortillas, flour tortillas</p></li>
<li><p>Low-sodium noodles</p></li>
<li><p>Low-sodium crackers and breadsticks</p></li>
<li><p>Unsalted popcorn, chips, pretzels</p></li>
</ul>
<p>Soups:</p>
<ul>
<li><p>Low-sodium canned and dehydrated soups, broth, bouillon</p></li>
<li><p>Homemade soups without added salt</p></li>
</ul>
<p>Condiments:</p>
<ul>
<li><p>Vinegar</p></li>
<li><p>Unsalted butter or margarine</p></li>
<li><p>Vegetable oils and low sodium sauces and salad dressings</p></li>
<li><p>Mayonnaise</p></li>
<li><p>All desserts made without salt</p></li>
</ul>
<h1 id="low-sodium-diet-foods-to-avoid">Low-sodium diet: foods to avoid</h1>
<p>Vegetables:</p>
<ul>
<li><p>Regular canned vegetables and canned vegetable juices</p></li>
<li><p>Olives, pickles, sauerkraut, and other pickled vegetables</p></li>
<li><p>Packaged mixes, such as scalloped or au gratin potatoes, frozen hash browns and Tater Tots</p></li>
<li><p>Commercially-prepared pasta sauces, tomato sauces, and salsa</p></li>
</ul>
<p>Proteins:</p>
<ul>
<li><p>Smoked, cured, salted or canned meat, fish or poultry including bacon, cold cuts, ham, frankfurters, sausage, sardines, caviar and anchovies</p></li>
<li><p>Frozen breaded meats and dinners, such as burritos and pizza</p></li>
<li><p>Canned entrees, such as ravioli, spam and chili</p></li>
<li><p>Nuts with salt added</p></li>
<li><p>Beans with salt added</p></li>
</ul>
<p>Dairy:</p>
<ul>
<li><p>Buttermilk</p></li>
<li><p>Regular and processed cheese, cheese spreads and sauces</p></li>
<li><p>Cottage cheese</p></li>
</ul>
<p>Grains etc.:</p>
<ul>
<li><p>Bread and rolls with salted tops</p></li>
<li><p>Quick breads, self-rising flour, biscuit, pancake and waffle mixes</p></li>
<li><p>Pizza, croutons, salted crackers</p></li>
<li><p>Prepackaged processed mixes for potatoes, rice, pasta and stuffing</p></li>
</ul>
<p>Soups:</p>
<ul>
<li><p>Regular canned and dehydrated soup, broth and bouillon</p></li>
<li><p>Cup of noodles, seasoned ramen mixes</p></li>
</ul>
<p>Condiments:</p>
<ul>
<li><p>Soy sauce, seasoning salt, other sauces, and marinades</p></li>
<li><p>Bottled salad dressings, regular salad dressing with bacon bits</p></li>
<li><p>Salted butter or margarine</p></li>
</ul>
<p>Desserts:</p>
<ul>
<li><p>Instant pudding and cake</p></li>
<li><p>Large portions of ketchup, mustard, sauces</p></li>
</ul>

