<h1 id="what-is-the-role-of-mast-cells-in-inflammation">What is the role
of mast cells in inflammation?</h1>
<p><a href="https://www.youtube.com/watch?v=7xB9Db-PHLI"
class="uri">https://www.youtube.com/watch?v=7xB9Db-PHLI</a> - By <a
href="https://www.youtube.com/@animatedbiologywitharpan">Animated
Biology with Arpan</a></p>
<p>In this video we’ll talk about mast cells, which are involved in the
allergic response…</p>
<p>Mast cells reside underneath the connective tissue. They contain
granules which are enriched in histamine and heparin, which are
important modulators in immunity…</p>
<p>They can be found in many epithelial layers. They can be found in
connective tissues. They are found in genital urinary tract and
respiratory epithelium…</p>
<p>Mature mast cells are highly associated with allergic responses. Mast
cells hav specific receptors known as FC receptors. These can bind to
IgE antibodies, so whenever there is an allergen-mediated exposure, mast
cells get sensitized and they degranulate. In these granules there are
several allergic mediators, so there are many physiological outcomes,
like bronchoconstriction, increased glandular secretion, smooth muscle
constriction, and vasodilation.</p>

