<h1 id="vitamin-k2-menaquinone">Vitamin K2 (menaquinone)</h1>
<p>Vitamin K2, also known as menaquinone, is one of the two primary forms of vitamin K, alongside vitamin K1 (phylloquinone). Like vitamin K1, vitamin K2 is a fat-soluble vitamin that plays essential roles in various bodily functions, including blood clotting, bone metabolism, and cardiovascular health.</p>
<p><strong>Key points</strong>:</p>
<ul>
<li><p><strong>Dietary Source</strong>: Primarily ound in animal products and fermented foods, as well as in smaller amounts in certain plant-based sources. Rich dietary sources of vitamin K2 include fermented foods like natto (a traditional Japanese dish made from fermented soybeans), cheese, egg yolks, and meats.</p></li>
<li><p><strong>Function</strong>: Plays a role in bone metabolism by activating osteocalcin, a protein involved in bone mineralization. Adequate vitamin K2 levels are associated with improved bone density and reduced risk of osteoporosis and fractures.</p></li>
<li><p><strong>Recommended dietary allowance</strong>: 90 micrograms (mcg) per day for women and 120 mcg per day for men.</p></li>
<li><p><strong>Safety</strong>: Deficiency is rare but can occur in individuals with malabsorption disorders or liver disease. Symptoms may include easy bruising, excessive bleeding, and impaired bone health. Toxicity is rare and typically occurs with high-dose supplementation; this can interfere with anticoagulant medications and may lead to blood clot formation.</p></li>
</ul>

