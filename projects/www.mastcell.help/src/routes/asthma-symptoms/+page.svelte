<h1 id="asthma-symptoms">Asthma symptoms</h1>
<p><a href="/asthma/">Asthma</a> symptoms can vary from person to person and can range from mild to severe. They are typically episodic, with periods of symptom flare-ups (asthma attacks) and times of improved lung function.</p>
<ul>
<li><p><strong>Coughing</strong>: A persistent cough, often worse at night or early in the morning, is a hallmark symptom of asthma. The cough may be dry or produce mucus.</p></li>
<li><p><strong>Wheezing</strong>: Wheezing is a high-pitched whistling sound that occurs when you breathe, particularly during exhalation. It is caused by the narrowing of the airways and the movement of air through constricted passages.</p></li>
<li><p><strong>Shortness of Breath</strong>: People with asthma may experience a sensation of breathlessness, as if they can’t get enough air. This is often associated with chest tightness.</p></li>
<li><p><strong>Chest Tightness</strong>: Asthma can cause a feeling of pressure or tightness in the chest. It may be described as a heavy or squeezing sensation.</p></li>
<li><p><strong>Increased Mucus Production</strong>: Asthma can lead to increased mucus production in the airways, which can contribute to coughing and difficulty breathing.</p></li>
<li><p><strong>Nighttime Changes</strong>: Asthma symptoms often worsen at night or in the early morning hours. This is known as “nocturnal asthma.” Nighttime symptoms can disrupt sleep and lead to daytime fatigue.</p></li>
<li><p><strong>Responses to Medication</strong>: A hallmark of asthma is that its symptoms often respond to bronchodilator medications, like albuterol. If your symptoms improve after using a bronchodilator, it can be an indication of asthma.</p></li>
</ul>

