<h1 id="anaphylaxis">Anaphylaxis</h1>
<p>Anaphylaxis is a medical term that describes a potentially life-threatening reaction affecting multiple systems in the body.
Anaphylaxis can lead to a sudden drop in blood pressure and airway constriction, making it hard to breathe. Common triggers are food allergies (such as peanuts), venom (such as bee stings), medications (such as penicillin), and chemicals (such as latex).</p>
<p><strong>Types</strong>:</p>
<ul>
<li><p><strong>Anaphylaxis</strong>: This is an allergic reaction that effects multiple body systems. It can vary in severity: see <a href="/anaphylaxis-severity/">anaphylaxis severity</a>.</p></li>
<li><p><strong>Anaphylactic shock</strong>: This is anaphylaxis that life-threatening. This must be treated immediately with a shot of adrenaline (a.k.a. epinephrine) and emergency medical care.</p></li>
<li><p><strong>Biphasic anaphylaxis</strong>: This means reactions come in two phases. The person may recover from the first reaction, but symptoms come back up to 12 hours later, and the person may need a second dose of adrenaline. This is why people carry two adrenaline shots.</p></li>
<li><p><strong>Protracted anaphylaxis</strong>: This means a persistent anaphylactic reaction that lasts hours to days without clearly resolving.</p></li>
<li><p><strong>Anaphylaxis-like a.k.a. anaphylactoid reaction</strong>: This means a reaction that is much like anaphylaxis, except without any identifiable allergen. See <a href="/anaphylactic-versus-anaphylactoid/">anaphylactic versus anaphylactoid</a>. Anaphylactoid shock must be treated immediately in the same way as anaphylactic shock. Biphasic anaphylactoid shock can happen. Protraced anaphylactoid reactions can happen.</p></li>
<li><p><strong>Idiopathic anaphylaxis</strong>: This is medical diagnosis when no specific trigger can be identified after an appropriate evaluation and when other conditions have been ruled out.</p></li>
</ul>

