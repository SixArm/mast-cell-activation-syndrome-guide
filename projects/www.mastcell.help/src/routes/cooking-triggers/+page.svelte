<h1 id="cooking-triggers">Cooking triggers</h1>
<p>Cooking triggers can include any of these:</p>
<ul>
<li><p><strong>What you cook</strong>: Foods, beverages, spices, and ingredients can contain histamine, or allergens, or cross-contaminations, or mast cell activation triggers that are independent of any allergies.</p></li>
<li><p><strong>Heat</strong>: Heat in the kitchen can be a trigger for mast cell activation. You can try using a portable kitchen fan, or a kitchen exhaust fan, or a stove-top hood fan.</p></li>
<li><p><strong>Gas</strong>: Gas from stoves can be a mast cell activation trigger. You may want to try switching from a gas stove to a portable induction stove, which can plug in to any typical outlet, and cook without using gas.</p></li>
<li><p><strong>Volatile organic compounds (VOCs)</strong>: Cooking can release VOCs that irritate airways and can trigger mast cell activation. You can try using a table-top VOC air filter to use right by the stove, or a VOC detector to use right by the stove.</p></li>
<li><p><strong>Microparticles</strong>: Many non-stick pans can release microparticles, such as microplastics and microshards of chemicals. For example, some low-budget non-stick pans can accumulate scratches that release microparticles that pollute your food. Some people get some relief by switching from low-budget non-stick pans to ferritic non-nickel stainless steel pans, which do not release microparticles, and are also nickle-free, and which work with gas stoves and induction stoves.</p></li>
</ul>

