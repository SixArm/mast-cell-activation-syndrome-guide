<h1 id="functional-medicine-timeline">Functional medicine timeline</h1>
<p>The functional medicine timeline is a comprehensive overview of a patientâ€™s health history, including key events, exposures, and experiences that may have influenced their current health status.</p>
<p><strong>Components</strong>:</p>
<ul>
<li><p><strong>Birth and early childhood</strong>: Birth weight, gestational age, mode of delivery, prenatal exposures, breastfeeding history, early milestones, and early illnesses.</p></li>
<li><p><strong>Medical history</strong>: Significant illnesses, injuries, surgeries, hospitalizations, and other medical events. May include diagnoses of chronic conditions, acute illnesses, and interventions.</p></li>
<li><p><strong>Family history</strong>: Genetic predispositions, hereditary conditions, and patterns of illness or disease within the family. May include details about the health status of close relatives.</p></li>
<li><p><strong>Lifestyle factors</strong>: Physical activity levels, sleep habits, stressors, coping strategies. May include tobacco use, alcohol consumption, recreational drug use.</p></li>
<li><p><strong>Environmental exposures</strong>: Occupational exposures, residential exposures (e.g., toxins, pollutants), travel history, and other environmental factors.</p></li>
<li><p><strong>Psychosocial history</strong>: Significant life events, relationships, social support, educational and occupational history, and emotional well-being. May include adversities, trauma, abuse.</p></li>
<li><p><strong>Dietary history</strong>: Nutrition, food preferences, patterns of intake, deficiencies, triggers, allergies, sensitivities, intolerances, restrictions.</p></li>
<li><p><strong>Supplement and medication history</strong>: Vitamins, minerals, herbs, medications, treatments. May include dosages, durations of use, and perceived benefits or side effects.</p></li>
</ul>

