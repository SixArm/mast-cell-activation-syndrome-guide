<h1 id="magnesium-supplements">Magnesium supplements</h1>
<p>Magnesium supplements are dietary supplements that contain magnesium. It is involved in over 300 biochemical reactions, including energy metabolism, protein synthesis, muscle and nerve function, blood glucose regulation, and blood pressure control.</p>
<p><strong>Key points</strong>:</p>
<ul>
<li><p><strong>Purpose</strong>: To treat magnesium deficiency, which can occur due to inadequate dietary intake, malabsorption, certain medical conditions, medications, or lifestyle factors.</p></li>
<li><p><strong>Forms</strong>: Magnesium supplements come in various forms, including magnesium oxide, magnesium citrate, magnesium glycinate, magnesium chloride, magnesium sulfate (Epsom salt), and magnesium hydroxide.</p></li>
<li><p><strong>Dosage</strong>: Recommended Dietary Allowance (RDA) is 410 milligrams (mg) per day for adult males, 310 mg per day for adult females (increasing during pregnancy and lactation).</p></li>
<li><p><strong>Indications</strong>: For individuals with diagnosed magnesium deficiency, or low magnesium such as due to low dietary intake, gastrointestinal disorders, kidney disease, diabetes, alcoholism, diuretics, or proton pump inhibitors.</p></li>
<li><p><strong>Absorption</strong>: Magnesium is absorbed in the small intestine, and absorption rates can be influenced by factors such as stomach acidity, food intake, and the presence of substances (such as fiber or phytates) that can inhibit absorption.</p></li>
<li><p><strong>Side Effects</strong>: Gastrointestinal symptoms such as diarrhea, nausea, and stomach cramps.</p></li>
<li><p><strong>Safety</strong>: Magnesium toxicity, known as hypermagnesemia, can cause symptoms such as diarrhea, nausea, vomiting, muscle weakness, and in severe cases, respiratory depression or cardiac arrest. Individuals with kidney disease should use caution.</p></li>
</ul>

