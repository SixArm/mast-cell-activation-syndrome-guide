<h1 id="multiple-antibiotic-resistant-coagulase-negative-staphylococci-marcons">Multiple Antibiotic Resistant Coagulase Negative Staphylococci (MARCoNS)</h1>
<p>Multiple Antibiotic Resistant Coagulase Negative Staphylococci (MARCoNS) is a term used to describe a group of bacteria that belong to the genus Staphylococcus and are resistant to multiple antibiotics.</p>
<p><strong>Key points:</strong></p>
<ul>
<li><p><strong>Staphylococcus Species:</strong> Staphylococci are a group of bacteria commonly found on the skin and mucous membranes. Some species are harmless; others can cause infections.</p></li>
<li><p><strong>Antibiotic Resistance:</strong> MARCoNS is of particular concern due to its resistance to commonly used antibiotics.</p></li>
<li><p><strong>Association with Chronic Infections:</strong> MARCoNS is often associated with chronic infections, including chronic sinus infections. It has been identified in some cases where individuals experience persistent or recurrent infections that are challenging to treat.</p></li>
<li><p><strong>Biofilm Formation:</strong> Staphylococci, including CoNS, are capable of forming biofilms. Biofilms are protective structures that bacteria create, making them more resistant to antibiotics and the immune system.</p></li>
<li><p><strong>Treatment Challenges:</strong> Due to the antibiotic resistance of MARCoNS and biofilm formation, treatment can be challenging. Providers may need to employ targeted antibiotic therapy based on sensitivity testing, and also address biofilm formation.</p></li>
</ul>

