<h1 id="physical-examination">Physical examination</h1>
<p>A physical examination is a comprehensive assessment of a person’s overall health status through observation, palpation, auscultation, and other diagnostic techniques. It an essential part of diagnosing medical conditions, monitoring health, and providing preventive care.</p>
<p>During a physical examination, the healthcare provider evaluates various aspects of the patient’s health, including their vital signs, general appearance, and specific organ systems.</p>
<ul>
<li><p><strong>Vital signs</strong>: blood pressure, heart rate, respiratory rate, and temperature.</p></li>
<li><p><strong>General appearance</strong>: posture, skin color, demeanor, distress, discomfort, or abnormal movements.</p></li>
<li><p><strong>Palpation</strong>: Use the hands to feel and examine various parts of the body, such as the abdomen, lymph nodes, and joints. Look for abnormalities such as swelling, tenderness, or masses.</p></li>
<li><p><strong>Auscultation</strong>: Use a stethoscope to listen to heart sounds, lung sounds, and bowel sounds.</p></li>
<li><p><strong>Assess</strong>: Assess the eyes, ears, nose, and throat for signs of infection, inflammation, or other abnormalities. Assess neurological function, including reflexes, sensation, coordination, and mental status.</p></li>
</ul>

