<h1 id="mast-cell-activation-syndrome-medications">Mast Cell Activation
Syndrome medications</h1>
<p>Mast Cell Activation Syndrome (MCAS) treatments can use medications
such as:</p>
<ul>
<li><p>Antihistamines: Non-sedating, second-generation antihistamines
like cetirizine (Zyrtec), loratadine (Claritin), or fexofenadine
(Allegra) are commonly used to block the effects of histamine and manage
symptoms such as itching, flushing, and hives.</p></li>
<li><p>Mast Cell Stabilizers: Medications like cromolyn sodium
(Gastrocrom) or ketotifen (Zaditor) can help stabilize mast cells and
reduce their reactivity.</p></li>
<li><p>Leukotriene Modifiers: Montelukast (Singulair) is a leukotriene
receptor antagonist that may be prescribed to manage respiratory and
other symptoms.</p></li>
<li><p>Proton Pump Inhibitors (PPIs): PPIs like omeprazole (Prilosec)
can help manage gastrointestinal symptoms by reducing stomach acid
production.</p></li>
<li><p>Adrenaline Auto-Injector: Individuals with MCAS who are at risk
of severe allergic reactions, including anaphylaxis, may be prescribed
an adrenaline (a.k.a. epinephrine) auto-injector and trained on how to
use it in case of an emergency.</p></li>
<li><p>Mast Cell Stabilizing Medications: In more severe cases of MCAS,
medications like tyrosine kinase inhibitors (e.g., imatinib) or anti-IgE
therapy (e.g., omalizumab) may be considered to manage symptoms and mast
cell activation.</p></li>
</ul>

