<h1 id="low-lectin-diet-foods-to-choose">Low-lectin diet: foods to choose</h1>
<p>Vegetables:</p>
<ul>
<li><strong>Leafy greens</strong>: Spinach, kale, lettuce, Swiss chard.</li>
<li><strong>Cruciferous vegetables</strong>: Broccoli, cauliflower, Brussels sprouts.</li>
<li>Zucchini and summer squash.</li>
<li>Bell peppers.</li>
</ul>
<p>Fruits:</p>
<ul>
<li>Bananas (ripe).</li>
<li>Blueberries.</li>
<li>Raspberries.</li>
<li>Strawberries.</li>
</ul>
<p>Animal Protein:</p>
<ul>
<li><strong>Meat</strong>: Beef, pork, lamb, poultry (chicken, turkey).</li>
<li><strong>Fish</strong>: Most types of fish.</li>
<li><strong>Seafood</strong>: Shrimp, crab, lobster.</li>
<li><strong>Eggs</strong>: Eggs are lectin-free.</li>
</ul>
<p>Animal Dairy:</p>
<ul>
<li>Lactose-free dairy products (e.g., lactose-free milk, yogurt, hard cheeses).</li>
<li>Butter and ghee are generally considered low in lectins.</li>
</ul>
<p>Grains and Pseudograins:</p>
<ul>
<li>White rice (long-grain and short-grain).</li>
<li>Quinoa (small servings, as it contains some lectins).</li>
<li>Oats (limited intake, preferably certified gluten-free).</li>
</ul>
<p>Legumes and Nuts:</p>
<ul>
<li>Peanuts (lightly roasted or boiled).</li>
<li>Cashews.</li>
<li>Macadamia nuts.</li>
<li>Sunflower seeds (hulled).</li>
<li>Almonds (after soaking and peeling, which reduces lectin content).</li>
</ul>
<p>Tubers and Root Vegetables:</p>
<ul>
<li>Sweet potatoes (moderation).</li>
<li>Yams (moderation).</li>
<li>Carrots.</li>
</ul>

