<h1 id="glossary-of-units-of-measurement">Glossary of units of measurement</h1>
<p><strong>Some common units</strong>:</p>
<ul>
<li><p><strong>g</strong>: gram is for mass</p></li>
<li><p><strong>l</strong>: litre is for liquid volume</p></li>
<li><p><strong>s</strong>: second is for time</p></li>
<li><p><strong>m</strong>: metre is for length</p></li>
<li><p><strong>A</strong>: ampere is for electric current</p></li>
<li><p><strong>K</strong>: kelvin is for temperature</p></li>
<li><p><strong>mol</strong>: mole is for amount of a substance</p></li>
<li><p><strong>cd</strong>: candela is for luminous intensity</p></li>
</ul>
<p><strong>Some common prefixes</strong>:</p>
<ul>
<li><p><strong>n</strong>: nano means 1/1000000000 a.k.a. <span class="math inline">10<sup>−9</sup></span></p></li>
<li><p><strong>μ</strong>: micro means 1/1000000 a.k.a. <span class="math inline">10<sup>−6</sup></span></p></li>
<li><p><strong>m</strong>: milli means 1/1000 a.k.a. <span class="math inline">10<sup>−3</sup></span></p></li>
<li><p><strong>k</strong>: kilo means 1000 a.k.a. <span class="math inline">10<sup>3</sup></span></p></li>
<li><p><strong>M</strong>: mega means 1000000 a.k.a. <span class="math inline">10<sup>6</sup></span></p></li>
<li><p><strong>G</strong>: giga means 1000000000 a.k.a <span class="math inline">10<sup>9</sup></span></p></li>
</ul>
<p><strong>Some common ratios</strong>:</p>
<ul>
<li><p><strong>g/L</strong>: grams per liter</p></li>
<li><p><strong>U/L</strong>: units per liter</p></li>
<li><p><strong>IU/L</strong>: international units per liter</p></li>
<li><p><strong>INR</strong>: international normalized ratio (measures blood clotting time)</p></li>
</ul>

