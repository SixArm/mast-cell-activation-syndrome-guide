<h1 id="bone-marrow-biopsy">Bone marrow biopsy</h1>
<p>A bone marrow biopsy is a medical procedure in which a small sample
of bone marrow tissue is extracted for examination. The bone marrow is
the spongy tissue found within the cavities of certain bones, such as
the hipbone, sternum (breastbone), and the long bones of the arms and
legs.</p>
<p>This procedure is typically performed by a hematologist, oncologist,
or a specially trained healthcare provider.</p>
<ul>
<li><p>Local anesthesia is administered to numb the skin and underlying
tissue, usually at the biopsy site. A special needle is inserted through
the skin and into the bone, and a syringe is used to aspirate (draw out)
a small amount of liquid bone marrow. A larger, hollow needle is used to
obtain a small core of bone marrow tissue. This may cause some pressure
or brief discomfort.</p></li>
<li><p>The collected samples are then sent to a laboratory for analysis
by pathologists and other specialists. Most patients can resume normal
activities shortly after. There may be some discomfort at the biopsy
site for a day or two.</p></li>
</ul>
<p>Bone marrow biopsies are performed for various purposes,
including:</p>
<ul>
<li><p>Diagnosis: A bone marrow biopsy may be conducted to diagnose and
evaluate a wide range of conditions, such as blood disorders (e.g.,
anemia, leukemia, lymphoma), bone marrow disorders, and certain
infectious diseases.</p></li>
<li><p>Staging and Monitoring: In individuals already diagnosed with
conditions like leukemia or lymphoma, a bone marrow biopsy can help
determine the extent of the disease and monitor treatment
response.</p></li>
<li><p>Identification of Cytogenetic Abnormalities: The bone marrow
sample can be used to analyze the genetic and chromosomal
characteristics of the bone marrow cells, which can be important for
treatment decisions and prognosis.</p></li>
</ul>

