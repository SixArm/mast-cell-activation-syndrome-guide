<h1 id="asthma-treatments">Asthma treatments</h1>
<p>Asthma is a chronic respiratory condition. Treatment typically involves a combination of medications.</p>
<p><strong>Quick-Relief (Rescue) Medications:</strong></p>
<ul>
<li><p><strong>Short-Acting Beta-Agonists (SABAs):</strong> Medications for asthma attacks relax the muscles around the airways, allowing them to open up. Example: albuterol.</p></li>
<li><p><strong>Ipratropium Bromide:</strong> This medication can also help relax the airways and is used in combination with SABAs for acute symptom relief.</p></li>
</ul>
<p><strong>Long-Term Control Medications:</strong></p>
<ul>
<li><p><strong>Inhaled Corticosteroids:</strong> These anti-inflammatory medications reduce airway inflammation. Examples: fluticasone, budesonide, beclomethasone.</p></li>
<li><p><strong>Long-Acting Beta-Agonists (LABAs):</strong> LABAs are often prescribed in combination with inhaled corticosteroids to improve lung function and provide additional symptom control. Examples: salmeterol, formoterol.</p></li>
<li><p><strong>Leukotriene Modifiers:</strong> These reduce inflammation and improve airflow in the airways. Example: montelukast.</p></li>
<li><p><strong>Theophylline:</strong> This oral medication can help open the airways and improve lung function. Itâ€™s used less frequently today due to potential side effects.</p></li>
</ul>
<p><strong>Biologics:</strong></p>
<ul>
<li><strong>Targeted therapies:</strong> In severe asthma cases, biologic medications may be considered that aim to address specific inflammatory pathways. Examples: omalizumab, mepolizumab, reslizumab, benralizumab.</li>
</ul>

