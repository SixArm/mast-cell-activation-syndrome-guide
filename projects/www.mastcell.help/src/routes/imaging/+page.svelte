<h1 id="imaging">Imaging</h1>
<p>Imaging techniques play a crucial role in modern medicine for diagnosing and monitoring various conditions.</p>
<p><strong>Typical types</strong>:</p>
<ul>
<li><p><strong>X-ray (Radiography)</strong>: X-rays use electromagnetic radiation to create images of the inside of the body. They are commonly used to visualize bones and detect fractures, dental problems, lung conditions, and certain tumors.</p></li>
<li><p><strong>Computed Tomography (CT) Scan</strong>: CT scans combine X-rays with computer technology to produce detailed cross-sectional images of the body. They are used to diagnose conditions such as internal injuries, tumors, infections, and bone disorders.</p></li>
<li><p><strong>Positron Emission Tomography (PET) Scan</strong>: PET scans involve injecting a small amount of radioactive material (tracer) into the body, which is taken up by tissues and organs. The scanner detects the radiation to create images. PET scans are used to evaluate organ function, detect cancer, assess brain disorders, and monitor the effectiveness of treatment.</p></li>
<li><p><strong>Magnetic Resonance Imaging (MRI)</strong>: MRI uses a strong magnetic field and radio waves to generate detailed images of the bodyâ€™s internal structures. It is particularly useful for imaging soft tissues. MRI is valuable for diagnosing conditions like brain tumors, spinal cord injuries, joint injuries, and multiple sclerosis.</p></li>
<li><p><strong>Magnetic resonance cholangiopancreatography (MRCP)</strong> MRCP visualizes the bile ducts and pancreatic ducts by using MRI.</p></li>
</ul>

