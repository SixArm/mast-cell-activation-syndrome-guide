<h1 id="burning-mouth-syndrome-bms">Burning mouth syndrome (BMS)</h1>
<p>Burning Mouth Syndrome (BMS) is a complex and often painful condition characterized by a burning sensation in the mouth, typically on the tongue, lips, palate, or throughout the entire oral cavity. This condition can also be referred to as “glossodynia”, “stomatodynia”, or “oral dysesthesia”. BMS is a chronic condition that can persist for months or even years.</p>
<p><strong>Key points</strong>:</p>
<ul>
<li><p><strong>Symptoms</strong>: Burning sensation in the mouth or other oral discomforts, such as dry mouth, altered taste (dysgeusia), or a metallic taste.</p></li>
<li><p><strong>Location</strong>: The burning sensation can affect various areas of the oral cavity, including the tongue, lips, palate, gums, and throat.</p></li>
<li><p><strong>Triggered by Eating</strong>: Symptoms may worsen or be triggered by eating or drinking, especially hot, spicy, or acidic foods and beverages.</p></li>
<li><p><strong>Causes</strong>: Potential factors include nerve damage, hormonal changes, nutritional deficiencies, oral habits, medications, allergies, and psychological factors.</p></li>
<li><p><strong>Diagnosis</strong>: Diagnosis of BMS is often based on the characteristic symptoms and exclusion of other possible causes of oral discomfort.</p></li>
<li><p><strong>Treatment</strong>: May include:oral medications (such as tricyclic antidepressants, anticonvulsants, or anti-anxiety medications) and oral rinses to relieve pain and dry mouth.</p></li>
<li><p><strong>Lifestyle Changes</strong>: Avoiding known triggers, staying hydrated, and practicing good oral hygiene.</p></li>
<li><p><strong>Diverse Population</strong>: BMS can affect people of all ages and genders, but it is more commonly seen in postmenopausal women.</p></li>
</ul>

