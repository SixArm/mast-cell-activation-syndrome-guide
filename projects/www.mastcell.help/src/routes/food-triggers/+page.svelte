<h1 id="food-triggers">Food triggers</h1>
<p>Foods triggers refer to adverse reactions triggered by exposure to certain foods. Individuals with food sensitivities may experience symptoms ranging from mild irritation to severe allergic reactions.</p>
<p><strong>Examples:</strong></p>
<ul>
<li><p><strong>Lactose:</strong> Can trigger GI bloating, gas, and diarrhea. Examples: milk, butter, cheese.</p></li>
<li><p><strong>Gluten:</strong> Can trigger GI discomfort, fatigue, headaches, and joint pain. Examples: wheat, barley, rye.</p></li>
<li><p><strong>FODMAPs:</strong> Fermentable Oligosaccharides, Disaccharides, Monosaccharides, and Polyols (FODMAPs) are carbohydrates, and can trigger bloating and abdominal pain.</p></li>
<li><p><strong>Food Additives:</strong> Artificial colors, flavors, and preservatives, can trigger hyperactivity, headaches, or GI issues.</p></li>
<li><p><strong>Histamine:</strong> Can trigger headaches, hives, and GI issues.</p></li>
<li><p><strong>Sulfites:</strong> Can trigger headaches, respiratory issues, and GI discomfort.</p></li>
<li><p><strong>Salicylates:</strong> Can trigger headaches and GI discomfort.</p></li>
<li><p><strong>Caffeine:</strong> Can trigger jitteriness, insomnia, and increased heart rate.</p></li>
<li><p><strong>Nightshades:</strong> Can trigger joint pain, rashes. Examples: tomatoes, peppers, eggplants.</p></li>
<li><p><strong>Shellfish:</strong> Can trigger mild skin reactions to severe anaphylaxis.</p></li>
<li><p><strong>Soy:</strong> Can trigger GI issues, skin reactions, and respiratory symptoms.</p></li>
<li><p><strong>Eggs:</strong> Can trigger skin rashes, respiratory symptoms, and GI issues.</p></li>
<li><p><strong>Nuts:</strong> Can trigger mild skin reactions to severe anaphylaxis.</p></li>
</ul>

