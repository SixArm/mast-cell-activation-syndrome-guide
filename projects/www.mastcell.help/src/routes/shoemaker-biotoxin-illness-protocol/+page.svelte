<h1 id="shoemaker-biotoxin-illness-protocol">Shoemaker Biotoxin Illness Protocol</h1>
<p>The Shoemaker Biotoxin Illness Protocol is a treatment approach developed by Dr. Ritchie Shoemaker for individuals with Chronic Inflammatory Response Syndrome (CIRS). CIRS is associated with exposure to biotoxins, particularly mycotoxins produced by molds. The protocol is designed to address the underlying environmental triggers, reduce inflammation, and support the body’s detoxification processes.</p>
<p><strong>Key steps</strong>:</p>
<ul>
<li><p><strong>Identification</strong>: Identify and address the source of biotoxin exposure, often related to water-damaged buildings with mold growth. Remediate the environment.</p></li>
<li><p><strong>Diagnosis</strong>: Blood tests identify biomarkers associated with inflammation and immune system dysfunction. Genetic tests looks for susceptibility markers, such as HLA-DR.</p></li>
<li><p><strong>Binders</strong>: Various binders, such as activated charcoal, cholestyramine, welchol, chlorella, and zeolite, may be recommended to help bind and eliminate biotoxins from the body.</p></li>
<li><p><strong>VIP (Vasoactive Intestinal Peptide)</strong>: VIP is a neuropeptide that plays a role in immune regulation. VIP may be administered to modulate the immune response and reduce inflammation. It is available in a synthetic form known as CirsVIP.</p></li>
<li><p><strong>Antifungal Medications</strong>: Antifungal medications may be prescribed to address fungal infections. These medications may include azoles such as fluconazole or itraconazole.</p></li>
<li><p><strong>Lifestyle Changes</strong>: Lifestyle modifications, including dietary changes, nutiritional supplements, stress management, and sufficient sleep, are emphasized as part of the protocol.</p></li>
</ul>

