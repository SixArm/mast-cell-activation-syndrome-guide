<h1 id="anaphylaxis-emergency-treatment">Anaphylaxis emergency
treatment</h1>
<p>Anaphylaxis emergency treatment in a pre-hospital setting:</p>
<ol type="1">
<li><p>Use emergency adrenaline auto-injector (a.k.a. brand name EpiPen)
and go immediately to a hospital emergency room.</p></li>
<li><p>Avoid triggers: remove trigger or remove self from
triggers.</p></li>
<li><p>Administer extra doses of H1 and H2 blockers: such as
ranitidine/cimetidine +
fexofenadine/hydroxyzine/diphenhydramine.</p></li>
<li><p>Administer corticosteroids because these may prevent prolonged
anaphylaxis treatment.</p></li>
<li><p>Treat H1 reactions (skin and respiratory: mucous overproduction,
asthma, throat tightening, skin itching):</p>
<ul>
<li><p>Stop asthma attack: albuterol sulfate, cromolyn sodium nebulized
and/or inhaled</p></li>
<li><p>Stop cough: cough drops</p></li>
<li><p>Clean up mucus: guaifenesin</p></li>
</ul></li>
<li><p>Treat H2 reactions (GI and cardiac: sudden diarrhea, gas,
bloating, vomiting, nausea, low blood pressure, heart palpitations,
difficulty breathing, feels like you may pass out):</p>
<ul>
<li><p>Stop H2 reaction: activated charcoal, cholestyramine, bland food
to soak up the irritant and prevent new reactions</p></li>
<li><p>Nausea: sea bands</p></li>
<li><p>Cardiac symptoms, including low blood pressure: electrolytes and
fluids, lay down, assess severity</p></li>
</ul></li>
<li><p>Manage H3 reactions (neurological: confusion, motor control loss,
fatigue, migraine, depression)</p>
<ul>
<li><p>Reduce likelihood of secondary injury: rest, lay down, do not
drive, do not operate heavy machinery</p></li>
<li><p>Reduce triggers: breathe clean fresh air; retreat to safe room or
environmental containment unit</p></li>
</ul></li>
</ol>

