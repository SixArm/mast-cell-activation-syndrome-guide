<h1 id="antimicrobial-herbs">Antimicrobial herbs</h1>
<p>Many herbs have been recognized for their potential antimicrobial properties, meaning they may have the ability to inhibit or kill microorganisms, including bacteria, viruses, fungi, and parasites.</p>
<p><strong>Examples</strong>:</p>
<ul>
<li><p><strong>Garlic</strong>: It contains allicin, a compound with antibacterial and antifungal effects.</p></li>
<li><p><strong>Oregano</strong>: It contains carvacrol and thymol, which have demonstrated antimicrobial properties. Oregano oil is known for antibacterial and antifungal effects.</p></li>
<li><p><strong>Turmeric</strong>: Its active compound curcumin may have antibacterial, antiviral, and antifungal effects.</p></li>
<li><p><strong>Ginger</strong>: It has been traditionally used for its medicinal properties. It may have antimicrobial activity against certain bacteria and viruses.</p></li>
<li><p><strong>Cinnamon</strong>: It contains compounds like cinnamaldehyde, which has demonstrated antimicrobial properties. It may have activity against bacteria, fungi, and viruses.</p></li>
<li><p><strong>Echinacea</strong>: is often used to support the immune system. Studies suggest it has antimicrobial and antiviral properties.</p></li>
<li><p><strong>Thyme</strong>: It contains thymol, a compound known for its antimicrobial properties. Thyme oil may have antibacterial and antifungal effects.</p></li>
<li><p><strong>Neem</strong>: It has a long history of use in traditional medicine, and may help against bacteria, viruses, and fungi.</p></li>
<li><p><strong>Goldenseal</strong>: It contains berberine, a compound with antimicrobial properties. It has been traditionally used to address infections.</p></li>
<li><p><strong>Tea Tree Oil</strong>: It is used topically for skin conditions. It may have antibacterial, antiviral, and antifungal effects.</p></li>
</ul>

