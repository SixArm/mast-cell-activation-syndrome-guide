<h1 id="never-bet-against-occam---by-lawrence-afrin">Never Bet Against
Occam - By Lawrence Afrin</h1>
<p>Never Bet Against Occam: Mast Cell Activation Disease and the Modern
Epidemics of Chronic Illness and Medical Complexity</p>
<p>By Lawrence B. Afrin M.D. (Author), Kendra Neilsen Myles (Editor),
Kristi Posival (Illustrator)</p>
<p><a
href="https://www.amazon.com/Never-Bet-Against-Occam-Activation/dp/0997319615">Amazon</a></p>
<p>In 2008 Dr. Afrin started coming to understand that a newly
recognized type of mast cell disease, now called mast cell activation
syndrome (MCAS), was the underlying diagnosis in many patients he was
seeing who were each suffering large assortments — quite different from
one patient to the next — of chronic multisystem inflammatory illnesses
of unclear cause. Dr. Afrin soon gained experience that MCAS is far more
prevalent than the only mast cell disease previously known to medicine
(the rare disease of mastocytosis) and that most MCAS patients, once
accurately diagnosed, can eventually find significantly helpful
medications targeted at the disease.</p>
<p>The frequency and magnitude of the improvements Dr. Afrin has seen —
even the relief that comes from finally having a unifying diagnosis
other than “psychosomatism” — have spurred him to focus in this area,
not only tending to the needs of his patients but also pursuing research
to advance our understanding of the disease and helping to educate other
professionals who in turn can help even more of the many people who have
long been suffering not only the symptoms of the disease but also the
natural concern of not understanding why one would be so “unlucky” to
have acquired so many medical problems. As it turns out, such patients
are not so unlucky and truly have just one root issue (and a very common
one at that), which has the biological capability to develop, directly
or indirectly, into most or all of their previously diagnosed
problems.</p>

