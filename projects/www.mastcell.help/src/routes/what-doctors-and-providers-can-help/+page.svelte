<h1 id="what-doctors-and-providers-can-help">What doctors and providers
can help?</h1>
<p>Mast Cell Activation Syndrome (MCAS) can be complex, and it often
requires healthcare providers who specialize in this rare and relatively
newly-recognized condition.</p>
<p>Here are some types of healthcare providers who can help:</p>
<ul>
<li><p><strong>Primary Care Physicians</strong>: Your primary care
doctor can serve as the first point of contact and may refer you to
specialists for further evaluation and management.</p></li>
<li><p><strong>Allergists/Immunologists</strong>: These specialists
often have expertise in diagnosing and managing immune system disorders
and allergies. They can help assess your symptoms, perform tests to
identify MCAS, and develop treatment plans.</p></li>
<li><p><strong>Rheumatologists</strong>: Some rheumatologists have
experience in diagnosing and treating systemic inflammatory reactions,
and especially ones affecting joints and connective tissues.</p></li>
<li><p><strong>Hematologists</strong>: Hematologists specialize in blood
disorders, and some are familiar with MCAS, as it can involve abnormal
blood cell counts and clotting issues.</p></li>
<li><p><strong>Gastroenterologists</strong>: MCAS can impact the
digestive system, leading to symptoms like abdominal pain and diarrhea.
Gastroenterologists can help assess and manage gastrointestinal symptoms
associated with MCAS.</p></li>
<li><p><strong>Dermatologists</strong>: Skin-related symptoms and
conditions can be a part of MCAS. Dermatologists can help with the
diagnosis and management of skin manifestations.</p></li>
<li><p><strong>Mast Cell Disorder Specialists</strong>: These experts
have in-depth knowledge and experience in diagnosing and managing
conditions like MCAS.</p></li>
</ul>

