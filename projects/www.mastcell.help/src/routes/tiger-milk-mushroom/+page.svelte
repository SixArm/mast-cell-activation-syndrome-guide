<h1 id="tiger-milk-mushroom">Tiger milk mushroom</h1>
<p>Tiger milk mushroom (a.k.a. Lignosus rhinocerus) is a species of mushroom that has been used in traditional medicine for its purported health benefits. It is native to Southeast Asia, particularly Malaysia, where it is commonly known as “Cendawan Susu Rimau”.</p>
<p><strong>Key points</strong>:</p>
<ul>
<li><p><strong>Traditional Use</strong>: It has been used by indigenous communities in Malaysia for centuries to treat various ailments, including coughs, asthma, fever, and general weakness.</p></li>
<li><p><strong>Bioactive Compounds</strong>: It contains bioactive compounds, including polysaccharides, proteins, terpenoids, steroids, phenolics, and lectins. Some of these compounds have demonstrated antioxidant, immunomodulatory, anti-inflammatory, and anticancer properties in laboratory studies.</p></li>
<li><p><strong>Antioxidant Activity</strong>: Certain compounds found in tiger milk mushroom, such as polysaccharides and phenolics, exhibit antioxidant activity, which may help protect cells from oxidative damage and reduce the risk of chronic diseases.</p></li>
<li><p><strong>Anti-inflammatory Effects</strong>: It may help reduce inflammation and alleviate symptoms of inflammatory conditions.</p></li>
</ul>

