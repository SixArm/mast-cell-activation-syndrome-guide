<h1 id="vitamin-b1-thiamine">Vitamin B1 = Thiamine</h1>
<p>Vitamin B1, also known as thiamine, is one of the eight B-vitamins essential for various bodily functions. Thiamine plays a crucial role in energy metabolism, as it is a coenzyme necessary for the conversion of carbohydrates into energy. It also helps maintain proper functioning of the nervous system.</p>
<p><strong>Key aspects</strong>:</p>
<ul>
<li><p><strong>Energy Metabolism</strong>: Thiamine is a coenzyme involved in several enzymatic reactions that help convert carbohydrates into adenosine triphosphate (ATP), the bodyâ€™s primary source of energy.</p></li>
<li><p><strong>Nervous System Function</strong>: Thiamine is important for the proper functioning of the nervous system. It plays a role in nerve signal transmission and can help protect nerve cells.</p></li>
<li><p><strong>Cardiovascular Health</strong>: Thiamine is associated with cardiovascular health and may help reduce the risk of heart diseases.</p></li>
<li><p><strong>Mental Function</strong>: Thiamine deficiency can lead to cognitive impairments and memory problems, including a condition known as Wernicke-Korsakoff syndrome.</p></li>
<li><p><strong>Red Blood Cell Formation</strong>: It is also involved in the production of red blood cells.</p></li>
</ul>
<p>Dietary sources of thiamine include whole grains, enriched cereals, beans, nuts, lean pork, and yeast. Thiamine is water-soluble, meaning that it is not stored in the body, and excess amounts are excreted in the urine.</p>
<p>Thiamine deficiency can lead to a condition known as beriberi, which is characterized by symptoms such as muscle weakness, nerve damage, and difficulty with basic bodily functions.</p>

