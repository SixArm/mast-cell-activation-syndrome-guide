<h1 id="health-intake">Health intake</h1>
<p>Health intake describes when a healthcare patient comes to a healthcare provider for the first time, and the patient and provider gather information about the patient’s health status, symptoms, history, and goals.</p>
<p><strong>Key aspects</strong>:</p>
<ul>
<li><p><strong>Physical examination</strong>: Assess all the body systems, such as the circulatory system (e.g. measure blood pressure and heart rate), reflex system (e.g. tap on the knee), airways system (e.g. take a deep breath), and so forth.</p></li>
<li><p><strong>History</strong>: Significant illnesses, injuries, symptoms, diagnoses, treatments, surgeries, hospitalizations, and other medical events.
May include broader aspects such as nutrition history, lifestyle history, occupational history, psychosocial history, family history.</p></li>
<li><p><strong>Diagnostics</strong>: Common diagnostic tests for new patient intake may include a complete blood count (CBC), 24-hour urine test, and more.</p></li>
<li><p><strong>Goals</strong>: Goals are the patient’s desired improvements. Ideally create goals that are SMART: specific, measurable, achievable, relevant, and timely.</p></li>
</ul>

