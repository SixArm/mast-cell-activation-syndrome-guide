<h1 id="isac-allergy-blood-test">ISAC allergy blood test</h1>
<p>ISAC is an allergy test that covers 112 allergenic components in food and airborne allergens. It is a screening test that gives all this information during one test. ISAC is a molecular blood test, can be done from a finger prick at home, or from a vein sampletaken by a phlebotomist in a blood laboratory.</p>
<p><strong>Key points</strong>:</p>
<ul>
<li><p><strong>What it covers</strong>: The test covers airborne allergens including animals, dust mites, grass, birch, moulds as well as food allergens. The full list of allergens tested and sample export can be found online.</p></li>
<li><p><strong>Microchip-based</strong>: The ISAC test is a blood test that uses microchip technology to detect specific IgE antibodies to food and airborne allergens (this test uses method called ELISA with a special reader; it replaced the test previously called RAST). It allows simultaneous measurement of specific antibodies to multiple allergen components in a single test, using only a few Âµl of serum or plasma. The allergens are pre-selected and include the most relevant specie specific and cross-reactive markers.</p></li>
<li><p><strong>No medication changes</strong>: The test result are not affected by antihistamines or your diet at the time of doing the tests.</p></li>
<li><p><strong>No fasting needed</strong>: Because the test is a molecular blood test, fasting is not required.</p></li>
</ul>

