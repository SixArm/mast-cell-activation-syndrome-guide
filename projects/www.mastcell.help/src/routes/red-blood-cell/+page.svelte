<h1 id="red-blood-cell-rbc">Red blood cell (RBC)</h1>
<p>Red blood cells (RBCs), also known as erythrocytes, are the most
abundant type of blood cell in the human body. They are essential for
the transport of oxygen from the lungs to the body’s tissues, and for
the transport of carbon dioxide, a waste product of metabolism, from the
tissues back to the lungs for exhalation.</p>
<p>Abnormalities in red blood cell production or function can lead to
various medical conditions, including anemia (a deficiency in the number
or function of red blood cells), polycythemia (an excess of red blood
cells), sickle cell anemia, and thalassemia.</p>
<p>Key aspects:</p>
<ul>
<li><p>Shape and Structure: Red blood cells are small, flexible, and
biconcave in shape, which provides a large surface area for the exchange
of gases and allows them to pass through tiny capillaries.</p></li>
<li><p>Hemoglobin: Hemoglobin is the protein that gives red blood cells
their red color. It binds to oxygen in the lungs to form oxyhemoglobin
and releases oxygen in the body’s tissues. Hemoglobin also helps carry
some carbon dioxide back to the lungs for exhalation.</p></li>
<li><p>Lifespan: Red blood cells are constantly produced in the bone
marrow, and typically circulate in the bloodstream for about 120 days.
Old and damaged red blood cells are removed from circulation by the
spleen and liver.</p></li>
<li><p>Production: The production of red blood cells is regulated by the
hormone erythropoietin, which is released by the kidneys in response to
low oxygen levels in the blood. This stimulates the bone marrow to
produce more red blood cells.</p></li>
<li><p>Hematocrit: Hematocrit is a blood test that measures the
proportion of red blood cells in the blood. It is expressed as a
percentage of the total blood volume.</p></li>
</ul>

