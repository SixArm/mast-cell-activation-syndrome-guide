<h1 id="data-protection">Data protection</h1>
<p>If a provider says “intake” or “history”, then ask them to put in writing that your data is solely for the provider for one-on-one treatment, and never for the provider’s vendors for advertising, marketing, promotion, aggregation, or anonymization.</p>
<ul>
<li><p>Good response: The provider puts it in writing. It’s fine if the provider can clarify that your data needs to go to/from your other direct providers and your direct insurance company. Epic is good.</p></li>
<li><p>Bad responses: The provider delays or declines. Any disclosures or consent forms or web sites or apps or terms that say advertising, or marketing, or promotion, or aggregation, or anonymization. SimplePractice and Elevate Health are bad.</p></li>
</ul>

