<h1 id="anaphylaxis-symptoms">Anaphylaxis symptoms</h1>
<p>Anaphylaxis is a severe allergic reaction that can occur rapidly after exposure to an allergen. Anaphylaxis can progress rapidly and can be fatal. Seek emergency help immediately.</p>
<p><strong>Skin Symptoms:</strong></p>
<ul>
<li><p>Hives (raised, itchy welts) on the skin</p></li>
<li><p>Generalized redness and flushing of the skin</p></li>
<li><p>Swelling, especially of the face, lips, tongue, and throat</p></li>
</ul>
<p><strong>Respiratory Symptoms:</strong></p>
<ul>
<li><p>Breathing difficulties, wheezing, stridor.</p></li>
<li><p>Coughing</p></li>
<li><p>Chest tightness</p></li>
</ul>
<p><strong>Cardiovascular Symptoms:</strong></p>
<ul>
<li><p>Rapid or weak pulse</p></li>
<li><p>Low blood pressure (which can lead to dizziness)</p></li>
<li><p>Paleness</p></li>
</ul>
<p><strong>Gastrointestinal Symptoms:</strong></p>
<ul>
<li><p>Nausea and vomiting</p></li>
<li><p>Abdominal pain or cramping</p></li>
<li><p>Diarrhea</p></li>
</ul>
<p><strong>Neurological Symptoms:</strong></p>
<ul>
<li><p>Anxiety or a sense of impending doom</p></li>
<li><p>Confusion</p></li>
<li><p>Loss of consciousness</p></li>
</ul>

