<h1 id="management-of-mast-cell-activation-syndrome-mcas---by-david-saperstein">Management of Mast Cell Activation Syndrome (MCAS) - By David Saperstein</h1>
<p><a href="https://www.youtube.com/watch?v=fvTIO-RTr9Y">Management of Mast Cell Activation Syndrome (MCAS) Presented by David Saperstein, M.D.</a></p>
<p><a href="https://www.youtube.com/@centerforcomplexneurology">Center for Complex Neurology, EDS &amp; POTS</a></p>
<p>Mast cell activation syndrome is a condition where the allergic system goes haywire. This occurs in many patients with EDS, POTS, or Dysautonomias. Individuals with MCAS are prone to unpredictable rashes, hives, or skin sensitivities.</p>
<p>MCAS can be hard to diagnose. There are blood and urine tests, but these are frequently normal. Through the use of <a href="/antihistamines/">antihistamines</a>, mast cell stabilizers, and other medications, the symptoms of MCAS can often be brought under control.</p>
<p>Dr. David Saperstein worked with a group of mast cell activation specialists, an effort led by Dr. Lawrence Afrin to propose new diagnostic criteria to update a Diagnosis Journal that was first published on January 22, 2014. This journal is called Diagnosis of Mast Cell Activation Syndrome: a Global “Consensus-2”. It is a must-read for all neurologists.</p>

