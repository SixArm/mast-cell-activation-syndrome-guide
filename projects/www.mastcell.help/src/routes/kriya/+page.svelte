<h1 id="kriya">Kriya</h1>
<p>Kriya refers to a set of yogic practices aimed at helping physical health, mental clarity, and spiritual growth.</p>
<p><strong>Types</strong>:</p>
<ul>
<li><p><strong>Physical Kriyas</strong>: These are yogic practices that involve physical movements or postures. They may include dynamic sequences of yoga asanas (poses) designed to improve flexibility, strength, and overall physical health. Examples: Surya Namaskar (Sun Salutations), specific sequences of hatha yoga poses, kundalini yoga exercises.</p></li>
<li><p><strong>Breathing Kriyas</strong>: Breathing exercises, known as pranayama, are an integral part of kriya yoga. These techniques involve controlling the breath to achieve specific effects on the body and mind. Examples: alternate nostril breathing (Nadi Shodhana), breath retention (Kumbhaka), rapid breathing (Bhastrika).</p></li>
<li><p><strong>Meditation Kriyas</strong>: Kriya meditation techniques are designed to facilitate inner stillness, mental clarity, and spiritual growth. Examples: visualization, concentration on specific energy centers (chakras), repeating mantras (chanting).</p></li>
<li><p><strong>Cleansing Kriyas</strong>: Kriya yoga also includes practices aimed at purifying the body and mind. Examples: Shankhaprakshalana (drinking saltwater and performing specific yoga poses), Neti (nasal cleansing), or Dhauti (cleansing of the digestive tract).</p></li>
<li><p><strong>Energy Kriyas</strong>: Kriyas can focus on the manipulation and cultivation of energy within the body, to help subtle energy channels (nadis) and centers (chakras) to balance and harmonize the flow of prana (life force energy). Examples: Kundalini awakening exercises, mudras (hand gestures), bandhas (energy locks).</p></li>
</ul>

