<h1 id="loratadine">Loratadine</h1>
<p>Loratadine is an antihistamine medication used to treat allergy symptoms.</p>
<p>It is available over-the-counter. Brand name: Claritin.</p>
<p><strong>Key points</strong>:</p>
<ul>
<li><p><strong>Class of Medication</strong>: Loratadine belongs to the second-generation antihistamines, which are less likely to cause drowsiness compared to first-generation antihistamines.</p></li>
<li><p><strong>Indications</strong>: Loratadine is commonly used to relieve symptoms associated with allergic conditions, including hay fever (allergic rhinitis), hives (urticaria), and other allergy symptoms. It helps alleviate symptoms such as sneezing, runny nose, itching, and watery eyes.</p></li>
<li><p><strong>Action</strong>: As an antihistamine, loratadine works by blocking the effects of histamine, a substance produced by the body during allergic reactions. It specifically targets the H1 receptors, reducing the symptoms associated with histamine release.</p></li>
<li><p><strong>Timing</strong>: Loratadine is known for its relatively long duration of action, providing relief from allergy symptoms for up to 24 hours.</p></li>
<li><p><strong>Side Effects</strong>: Generally mild and may include headache, dry mouth, fatigue, and gastrointestinal symptoms.</p></li>
<li><p><strong>Precautions</strong>: Exercise caution when engaging in activities that require alertness, especially when first starting the medication.</p></li>
</ul>

