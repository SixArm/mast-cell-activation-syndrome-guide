<h1 id="mast-cell-activation-syndrome-mcas-diagnosis-consensus-2-criteria">Mast Cell Activation Syndrome (MCAS): diagnosis consensus-2 criteria</h1>
<p><a href="https://www.degruyter.com/document/doi/10.1515/dx-2020-0005/html">Source</a>. Diagnosis established upon demonstration of the major criterion combined with at least one minor criterion (and absence of any other disease better accounting for the problems).</p>
<p><strong>Major Criterion:</strong></p>
<ol type="1">
<li>Constellation of clinical complaints attributable to pathologically increased MC activity (MC mediator release syndrome)</li>
</ol>
<p><strong>Minor Criteria:</strong></p>
<ol type="1">
<li><p>Multifocal or disseminated infiltrates of MCs in marrow and/or extracutaneous organ(s) (e.g., gastrointestinal or genitourinary tract; &gt;19 MCs/high power field)</p></li>
<li><p>Abnormal spindle-shaped morphology in &gt;25% of MCs in marrow or other
extracutaneous organ(S)</p></li>
<li><p>Abnormal MC expression of CD2 and/or CD25 (i.e., co-expression of CD117/CD25 or
CD117/CD2)</p></li>
<li><p>MC genetic changes (e.g., activating KIT codon 419, 509 or 560 mutations) shown to increase MC activity</p></li>
<li><p>Evidence (typically from body fluids such as whole blood, serum, plasma, or urine) of
above-normal levels of MC mediators including: tryptase, histamine or its metabolites (e.g., N-methylhistamine), heparin, chromogranin A (note potential confounders of cardiac or renal failure, neuroendocrine tumors, recent proton pump inhibitor use, or chronic atrophic gastritis), other relatively MC-specific mediators (e.g., ecosanoids including prostaglandin (PG) D2, its metabolite 11-B-PGF2a, or leukotriene E4)</p></li>
<li><p>Symptomatic response to inhibitors of MC activation or MC mediator production or action.</p></li>
</ol>

