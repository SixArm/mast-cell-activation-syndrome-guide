<p><a href="https://www.youtube.com/@TheEhlersDanlosSociety">The Ehlers-Danlos Society</a></p>
<p>Kate Robinson joins us at the 2022 EDS ECHO Summit Series: Allergy &amp; Immunology Complications to share her experience of living with hypermobile Ehlers-Danlos syndrome (hEDS), mast cell activation syndrome (MCAS), and following a low histamine diet.</p>
<p>Kate manages a low histamine cooking blog and private forum for others with Ehlers-Danlos syndromes (EDS), postural orthostatic tachycardia syndrome (POTS), and MCAS.</p>
<p>The Ehlers-Danlos Society is the global nonprofit organization dedicated to saving and improving the lives of those affected by the Ehlers-Danlos syndromes (EDS), hypermobility spectrum disorders (HSD), and related conditions.</p>

