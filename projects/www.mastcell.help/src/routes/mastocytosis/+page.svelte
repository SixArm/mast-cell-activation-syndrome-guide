<h1 id="mastocytosis">Mastocytosis</h1>
<p>Mastocytosis is a rare disorder characterized by the abnormal accumulation and proliferation of mast cells in various tissues throughout the body. Mast cells are immune cells that are involved in the bodyâ€™s response to allergens and immune challenges. In mastocytosis, these cells are overproduced and abnormally shaped.</p>
<p><strong>Two main forms</strong>:</p>
<ul>
<li><p><strong>Cutaneous mastocytosis</strong>: This primarily affects the skin and is more common in children.</p></li>
<li><p><strong>Systemic mastocytosis</strong>: This affects the skin and also other organs and tissues in the body.</p></li>
</ul>
<p>Symptoms of mastocytosis can vary widely. Common symptoms may include skin rashes, itching, flushing, abdominal pain, diarrhea, nausea, vomiting, and in some cases, more severe systemic symptoms, such as anaphylaxis.</p>
<p>Diagnosing mastocytosis typically involves a combination of clinical evaluation, <a href="/physical-examination/">physical examination</a>, laboratory tests to measure mast cell mediators, skin biopsies, and sometimes bone marrow biopsies.</p>
<p>Management and treatment depend on the subtype and severity of mastocytosis. For some individuals with indolent disease, management may focus on controlling symptoms and avoiding triggers, while aggressive forms may require more intensive treatments, such as targeted therapy or bone marrow transplant in rare cases.</p>

