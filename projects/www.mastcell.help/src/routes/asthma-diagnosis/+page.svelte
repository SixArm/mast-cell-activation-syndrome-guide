<h1 id="asthma-diagnosis">Asthma diagnosis</h1>
<p>The diagnosis of asthma typically involves a combination of medical
history, physical examination, and various diagnostic tests.</p>
<p>Key points:</p>
<ul>
<li><p>Medical History: A provider will take a detailed medical history,
including information about your symptoms and factors that affect your
breathing difficulties.</p></li>
<li><p>Physical Examination: A provider will assess your lung function
and look for signs of asthma, such as wheezing or prolonged
exhalation.</p></li>
<li><p>Rule Outs: A provider will rule out other medical conditions that
can cause similar symptoms, such as chronic obstructive pulmonary
disease (COPD), allergic bronchopulmonary aspergillosis, or cardiac
conditions that affect breathing.</p></li>
</ul>
<p>Diagnostic tests:</p>
<ul>
<li><p>Spirometry: This test helps assess lung capacity and airflow
obstruction. You will take a deep breath and then blow forcefully into a
tube. This measures how much air you can exhale and how quickly you can
do it.</p></li>
<li><p>Peak Flow Measurement: This portable device measures how fast you
can exhale.</p></li>
<li><p>Bronchoprovocation testing: This test involves inhaling a
substance that can trigger bronchoconstriction, which is a hallmark of
asthma.</p></li>
<li><p>Positive response to medication: If you use a bronchodilator and
you improve, this suggests that you may have asthma. This is often
referred to as a “reversible” or “treatable” airway
obstruction.</p></li>
<li><p>Allergy testing: Allergy skin testing or blood tests may be
conducted to determine if allergens are contributing to your
symptoms.</p></li>
</ul>

