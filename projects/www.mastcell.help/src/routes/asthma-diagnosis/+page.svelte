<h1 id="asthma-diagnosis">Asthma diagnosis</h1>
<p>The diagnosis of asthma typically involves a combination of medical history, physical examination, and various diagnostic tests.</p>
<p><strong>Key points</strong>:</p>
<ul>
<li><p><strong>Medical History</strong>: A provider will take a detailed medical history, including information about your symptoms and factors that affect your breathing difficulties.</p></li>
<li><p><strong>Physical Examination</strong>: A provider will assess your lung function and look for signs of asthma, such as wheezing or prolonged exhalation.</p></li>
<li><p><strong>Rule Outs</strong>: A provider will rule out other medical conditions that can cause similar symptoms, such as chronic obstructive pulmonary disease (COPD), allergic bronchopulmonary aspergillosis, or cardiac conditions that affect breathing.</p></li>
</ul>
<p>Diagnostic tests:</p>
<ul>
<li><p><strong>Spirometry</strong>: This test helps assess lung capacity and airflow obstruction. You will take a deep breath and then blow forcefully into a tube. This measures how much air you can exhale and how quickly you can do it.</p></li>
<li><p><strong>Peak Flow Measurement</strong>: This portable device measures how fast you can exhale.</p></li>
<li><p><strong>Bronchoprovocation testing</strong>: This test involves inhaling a substance that can trigger bronchoconstriction, which is a hallmark of asthma.</p></li>
<li><p><strong>Positive response to medication</strong>: If you use a bronchodilator and you improve, this suggests that you may have asthma. This is often referred to as a “reversible” or “treatable” airway obstruction.</p></li>
<li><p><strong>Allergy testing</strong>: Allergy skin testing or blood tests may be conducted to determine if allergens are contributing to your symptoms.</p></li>
</ul>

