<h1 id="asthma-diagnosis">Asthma diagnosis</h1>
<p><a href="/asthma/">Asthma</a> diagnosis typically involves a combination of <a href="/medical-history/">medical history</a>, <a href="/physical-examination/">physical examination</a>, and <a href="/diagnostic-tests/">diagnostic tests</a>.</p>
<p><strong>Key points</strong>:</p>
<ul>
<li><p><strong>Medical History</strong>: A provider gathers information about your symptoms and factors that affect your breathing difficulties.</p></li>
<li><p><strong>Physical Examination</strong>: A provider assesses your lung function, and looks for signs of asthma, such as wheezing.</p></li>
<li><p><strong>Rule Outs</strong>: A provider rules out other conditions, such as chronic obstructive pulmonary disease (COPD), allergic bronchopulmonary aspergillosis, or cardiac conditions that affect breathing.</p></li>
</ul>
<p><strong>Diagnostic tests</strong>:</p>
<ul>
<li><p><strong>Spirometry</strong>: You will take a deep breath and then blow forcefully into a tube. This measures how much air you can exhale and how quickly you can do it.</p></li>
<li><p><strong>Peak Flow Measurement</strong>: This portable device measures how fast you can exhale.</p></li>
<li><p><strong>Bronchoprovocation testing</strong>: This test involves inhaling a substance that can trigger bronchoconstriction, which is a hallmark of asthma.</p></li>
<li><p><strong>Positive response to medication</strong>: If you use a bronchodilator and you improve, this suggests that you may have asthma. This is often referred to as a “reversible” or “treatable” airway obstruction.</p></li>
<li><p><strong>Allergy testing</strong>: Allergy skin testing or blood tests may be conducted to determine if allergens are contributing to your symptoms.</p></li>
</ul>

