<h1 id="glossary-of-medical-tests">Glossary of medical tests</h1>
<p><strong>Blood work</strong>:</p>
<ul>
<li><p><strong>CBC:</strong> complete blood count</p></li>
<li><p><strong>FBC:</strong> full blood count</p></li>
</ul>
<p><strong>Common tests:</strong></p>
<ul>
<li><p><strong>LFT:</strong> liver function test</p></li>
<li><p><strong>ECG:</strong> electrocardiogram</p></li>
<li><p><strong>EEG:</strong> electroencephalogram</p></li>
<li><p><strong>EMG:</strong> electromyogram</p></li>
</ul>
<p><strong>Imaging:</strong></p>
<ul>
<li><p><strong>CT:</strong> computerized tomography</p></li>
<li><p><strong>MRI:</strong> magnetic resonance imaging</p></li>
<li><p><strong>PET:</strong> positron emission tomography</p></li>
</ul>
<p><strong>Markers:</strong></p>
<ul>
<li><p><strong>ABG:</strong> arterial blood gas</p></li>
<li><p><strong>BMR:</strong> basal metabolism rate</p></li>
<li><p><strong>BP:</strong> blood pressure</p></li>
<li><p><strong>CRP:</strong> <a href="/c-reactive-protein">C-reactive protein (CRP)</a> blood test marker of inflammation</p></li>
<li><p><strong>Hb:</strong> hemoglobin (a substance in red blood cells that moves oxygen around the body)</p></li>
<li><p><strong>RBC:</strong> red blood cell</p></li>
<li><p><strong>WBC:</strong> white blood cell</p></li>
<li><p><strong>LDL:</strong> low-density lipoprotein (a.k.a. bad cholesterol)</p></li>
<li><p><strong>HDL:</strong> high-density lipoprotein (a.k.a. good cholesterol)</p></li>
</ul>

