<h1 id="water-treatment-home-tips">Water treatment home tips</h1>
<p>Water treatment at home uses a water filter. Here are tips that may help you.</p>
<p><strong>Testing</strong>:</p>
<ul>
<li><p>If you have an aquarium shop close, you can tell them you are thinking of setting up an aquarium tank</p></li>
<li><p>Take a clean glass bottle of water that has come from the closest source of your water main, and a clean glass bottle of water from your kitchen tap. The two samples can check for an isolated problem with your pipes.</p></li>
<li><p>You at least want to know pH, ammonia, nitrites, nitrates. This will straight up off the bat let you know if your water is appropriate for you to drink.</p></li>
</ul>
<p><strong>Installation</strong>:</p>
<ul>
<li><p>Ideally get a 3 stage filtration system, or better, at least for your drinking water. Ideally go below 4 microns, because that’s when the filter protects you from chlorine and chloramines.</p></li>
<li><p>If you are installing an under-sink water filter, and you don’t want to drill through your sink top, then here’s a tip: if you know someone with a 3D printer, then you can ask them to print you a filter mounting bracket that can go under your sink. This is handy for people who rent or who move often.</p></li>
</ul>

