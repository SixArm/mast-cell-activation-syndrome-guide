<h1 id="low-sodium-diet-foods-to-avoid">Low-sodium diet: foods to
avoid</h1>
<p>Vegetables:</p>
<ul>
<li><p>Regular canned vegetables and canned vegetable juices</p></li>
<li><p>Olives, pickles, sauerkraut, and other pickled
vegetables</p></li>
<li><p>Packaged mixes, such as scalloped or au gratin potatoes, frozen
hash browns and Tater Tots</p></li>
<li><p>Commercially-prepared pasta sauces, tomato sauces, and
salsa</p></li>
</ul>
<p>Proteins:</p>
<ul>
<li><p>Smoked, cured, salted or canned meat, fish or poultry including
bacon, cold cuts, ham, frankfurters, sausage, sardines, caviar and
anchovies</p></li>
<li><p>Frozen breaded meats and dinners, such as burritos and
pizza</p></li>
<li><p>Canned entrees, such as ravioli, spam and chili</p></li>
<li><p>Nuts with salt added</p></li>
<li><p>Beans with salt added</p></li>
</ul>
<p>Dairy:</p>
<ul>
<li><p>Buttermilk</p></li>
<li><p>Regular and processed cheese, cheese spreads and sauces</p></li>
<li><p>Cottage cheese</p></li>
</ul>
<p>Grains etc.:</p>
<ul>
<li><p>Bread and rolls with salted tops</p></li>
<li><p>Quick breads, self-rising flour, biscuit, pancake and waffle
mixes</p></li>
<li><p>Pizza, croutons, salted crackers</p></li>
<li><p>Prepackaged processed mixes for potatoes, rice, pasta and
stuffing</p></li>
</ul>
<p>Soups:</p>
<ul>
<li><p>Regular canned and dehydrated soup, broth and bouillon</p></li>
<li><p>Cup of noodles, seasoned ramen mixes</p></li>
</ul>
<p>Condiments:</p>
<ul>
<li><p>Soy sauce, seasoning salt, other sauces, and marinades</p></li>
<li><p>Bottled salad dressings, regular salad dressing with bacon
bits</p></li>
<li><p>Salted butter or margarine</p></li>
</ul>
<p>Desserts:</p>
<ul>
<li><p>Instant pudding and cake</p></li>
<li><p>Large portions of ketchup, mustard, sauces</p></li>
</ul>

