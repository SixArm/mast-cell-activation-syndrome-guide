<h1 id="chlorpheniramine">Chlorpheniramine</h1>
<p>Chlorpheniramine is an antihistamine medication commonly used to relieve symptoms of allergy, hay fever, and the common cold.</p>
<p>It is available over-the-counter. Brand name: Chlor-Trimeton.</p>
<p><strong>Key points</strong>:</p>
<ul>
<li><p><strong>Class of Medication</strong>: It belongs to the class of first-generation antihistamines. It works by blocking the effects of histamine, a substance produced by the body during allergic reactions.</p></li>
<li><p><strong>Indications</strong>: It is used to relieve symptoms associated with allergic conditions, such as sneezing, itching, watery eyes, and runny nose. It can also help alleviate symptoms of the common cold.</p></li>
<li><p><strong>Action</strong>: It blocks the H1 receptors, preventing the binding of histamine. This action reduces the symptoms of allergic reactions.</p></li>
<li><p><strong>Side Effects</strong>: May include drowsiness, dizziness, dry mouth, blurred vision, or constipation. Older adults may be more sensitive to certain side effects, such as dizziness and urinary retention.</p></li>
<li><p><strong>Interactions</strong>: It may interact with alcohol and with other medications, including certain antidepressants and sedatives.</p></li>
<li><p><strong>Contraindications</strong>: Individuals with certain medical conditions, such as glaucoma, an enlarged prostate, or urinary retention, should consult with a healthcare professional.</p></li>
</ul>

