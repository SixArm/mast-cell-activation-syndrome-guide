<h1 id="allergy-types">Allergy types</h1>
<p>Allergies are a common and chronic condition in which the immune
system reacts to substances that are typically harmless. These
substances, known as allergens, can trigger an allergic response in
sensitive individuals.</p>
<p>Common allergy types:</p>
<ul>
<li><p>Hay fever (allergic rhinitis) is triggered by airborne allergens
such as pollen. Symptoms include sneezing, runny or stuffy nose, itchy
or watery eyes, and coughing.</p></li>
<li><p>Asthma: Allergens, infections, or irritants can trigger wheezing,
shortness of breath, chest tightness, and coughing.</p></li>
<li><p>Allergic bronchitis is an allergic reaction in the bronchial
tubes that can lead to coughing, increased mucus production, and chest
discomfort.</p></li>
<li><p>Food allergies can be triggered by common food allergens such as
nuts, eggs, milk, soy, wheat, fish, and shellfish. Symptoms range from
mild hives to severe anaphylaxis, which is potentially fatal.</p></li>
<li><p>Insect sting allergies can be triggered by stings or bites, such
as from bees, wasps, or ants. Symptoms can include swelling, hives,
difficulty breathing, and anaphylaxis.</p></li>
<li><p>Medication allergy can be triggered by antibiotics, non-steroidal
anti-inflammatory drugs (NSAIDs), or aspirin. Symptoms range from mild
skin rashes to severe anaphylaxis.</p></li>
<li><p>Latex allergy can be triggered by exposure to latex-containing
products, such as gloves or balloons. Symptoms may include skin
reactions or respiratory problems.</p></li>
<li><p>Contact dermatitis is when contact with environmental allergens
can cause redness, itching, and a rash.</p></li>
<li><p>Atopic dermatitis (eczema) is chronic skin condition
characterized by red, itchy, and inflamed skin, often related to
allergic triggers.</p></li>
</ul>

