<h1 id="glossary-of-medical-time">Glossary of medical time</h1>
<p><strong>When to do treatments:</strong></p>
<ul>
<li><strong>ac:</strong> “ante cibum” means before meals</li>
<li><strong>pc:</strong> “psot cibum” means after meals</li>
<li><strong>am</strong>: “ante-meridian” means before noon, such as morning</li>
<li><strong>pm</strong>: “post-meridian” means after noon, such as evening</li>
<li><strong>prn</strong>: “pro re nata” means as the thing is needed</li>
<li><strong>ud</strong>: “ut dictum” means as directed</li>
<li><strong>qs</strong>: “quantum sufficit” means as much as is sufficient</li>
</ul>
<p><strong>When to repeat treatments:</strong></p>
<ul>
<li><strong>o:</strong> once</li>
<li><strong>q:</strong> every</li>
<li><strong>d</strong>: day</li>
<li><strong>h</strong>: hour</li>
<li><strong>od</strong>: once per day</li>
<li><strong>oh</strong>: once per hour</li>
<li><strong>o2d</strong>: once per 2 days</li>
<li><strong>o2h</strong>: once per 2 hours</li>
<li><strong>qd:</strong> every day</li>
<li><strong>qh:</strong> every hour</li>
<li><strong>q2d:</strong> every 2 days</li>
<li><strong>q2h:</strong> every 2 hours</li>
<li><strong>qod</strong>: every other day a.k.a. alternating days</li>
<li><strong>qoh</strong>: every other hour a.k.a. alternating hours</li>
</ul>
<p><strong>How many items:</strong></p>
<ul>
<li><strong>i:</strong> 1 tablet, pill, item, etc.</li>
<li><strong>ii:</strong> 2 tablets, pills, items, etc.</li>
<li><strong>iii:</strong> 3 tablets, pills, items, etc.</li>
</ul>

