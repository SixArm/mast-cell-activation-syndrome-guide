<h1 id="cutaneous-mastocytosis-cm">Cutaneous mastocytosis (CM)</h1>
<p>Cutaneous mastocytosis (CM) primarily affects the skin and is more common in children.</p>
<p>Cutaneous mastocytosis includes conditions such as:</p>
<ul>
<li><p><strong>Urticaria Pigmentosa</strong>: This is the most common form of cutaneous mastocytosis and typically appears as reddish-brown patches or small raised bumps on the skin.</p></li>
<li><p><strong>Diffuse Cutaneous Mastocytosis</strong>: This form is more extensive and covers larger areas of the skin.</p></li>
<li><p><strong>Telangiectasia Macularis Eruptiva Perstans (TMEP)</strong>: This is characterized by reddish-brown macules or small bumps, often with telangiectasia (dilated blood vessels) on the skin.</p></li>
</ul>

