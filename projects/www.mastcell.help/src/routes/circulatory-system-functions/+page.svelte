<h1 id="circulatory-system-functions">Circulatory system functions</h1>
<p>The functions of the circulatory system include:</p>
<ul>
<li><p>Transportation: It transports oxygen from the lungs to body
tissues and carbon dioxide from tissues to the lungs for elimination. It
also carries nutrients from the digestive system to cells and removes
metabolic waste products.</p></li>
<li><p>Immune Response: White blood cells in the circulatory system help
protect the body against infections and foreign invaders.</p></li>
<li><p>Hormone Distribution: Hormones produced by endocrine glands are
released into the bloodstream and distributed throughout the body,
regulating various physiological processes.</p></li>
<li><p>Temperature Regulation: Blood helps distribute heat throughout
the body, maintaining a stable core body temperature.</p></li>
<li><p>Clotting: Platelets and clotting factors in the blood are
essential for stopping bleeding when blood vessels are damaged.</p></li>
<li><p>Regulation of Blood Pressure: The circulatory system helps
regulate blood pressure, ensuring that blood flows at an appropriate
pressure to reach all parts of the body.</p></li>
</ul>

