<h1 id="avocado-allergy">Avocado allergy</h1>
<p><a href="https://www.allergycliniclondon.co.uk/three-less-common-food-allergies-you-might-not-know-about/">Source</a></p>
<p>Turns out avocado allergies are actually a thing! And this specific allergen is two-fold, making it even more complicated.</p>
<p>You can have an oral allergy to avocados as they’re high in histamines. You eat an avocado, your body reacts, and your immune system tries to destroy it. The symptoms tend to include itching in your lips, mouth, or throat. It can also lead to stomach issues similar to IBS.</p>
<p>Now onto part two. The proteins found in avocados are really similar to those in latex. And this means you might react to avocados if you’re allergic to latex. You could get swollen lips, watery eyes, a runny nose, and stomach ache. In more severe cases, you might also experience hives or anaphylaxis.</p>

