<h1 id="antihistamines-and-mast-cell-stabilizers">Antihistamines and mast cell stabilizers</h1>
<p>There are some medications that are primarily antihistamines, plus also have
additional effects that can help mast cell disorder patients. If you’re able to
choose which antihistamines you use, then you may want to favor trying the ones
with mast cell stabilizer effects:</p>
<ul>
<li><p><a href="/diphenhydramine/">diphenhydramine</a> (brand name Benadryl)</p></li>
<li><p><a href="/cetirizine/">cetirizine</a> (brand name Zyrtec) is an H1 antihistamine and can
stabilize mast cells. It also can block influx of eosinophils and blocks
generation of leukotrine C4.</p></li>
<li><p><a href="/levocetirizine/">levocetirizine</a> (brand name Xyzal) is an H1
antihistamine. Primarily used for allergic rhinitis, including allergy
symptoms such as watery eyes, runny nose, sneezing, hives, and itching.</p></li>
<li><p><a href="/ketotifen/">ketotifen</a> (brand names Zaditen, Zaditor, Alaway). Primarily
used to treat allergic conditions such as conjunctivitis, asthma, and
urticaria. Increasingly used for mast cell stabilizing properties.</p></li>
<li><p><a href="/epinastine/">epinastine</a> (brand names Alesion, Elestat, Purivist,
Relestat)</p></li>
<li><p><a href="/azelastine/">azelastine</a> (brand names Astepro, Astelin)</p></li>
<li><p><a href="/olopatadine/">olopatadine</a> (brand names: Patanase, Pataday, Pazeo)</p></li>
<li><p><a href="/alcaftadine/">alcaftadine</a> (brand name: Lastacaft)</p></li>
</ul>
<p>If you’re trying to convince your doctor that you may have a mast cell disorder,
rather than solely allergies, then you may want to consider trying a “test of
treatment” where you try an H1 blocker with mast cell stabilizer effects for 2
weeks, then an H1 blocker without mast cell stabilizer effects for 2 weeks. If
you feel better on the first one, then that’s evidence you have a mast cell
problem.</p>
<p>Other mast cell stabilizers include
<a href="/vitamin-c-ascorbic-acid/">vitamin C</a>,
<a href="/diamine-oxidase/">diamine oxidase</a>,
<a href="/quercetin/">quercetin</a>,
<a href="/cromolyn-sodium/">cromolyn sodium</a>,
<a href="/omalizumab/">omalizumab</a>,
<a href="/hydroxychloroquine/">hydroxychloroquine</a>,
<a href="/medical-marijuana/">medical marijuana</a>.</p>

