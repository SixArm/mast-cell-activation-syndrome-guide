<h1 id="low-salicylate-diet-foods-to-avoid">Low-salicylate diet: foods
to avoid</h1>
<p>For a low-salicylate diet, here are items to avoid or limit:</p>
<ul>
<li><p>High-salicylate fruits and vegetables.</p></li>
<li><p>Spices and herbs, especially those with strong flavors.</p></li>
<li><p>High-salicylate nuts, like almonds and peanuts.</p></li>
<li><p>Some grains, like wheat and rye.</p></li>
<li><p>Caffeinated beverages, alcohol, and certain food
additives.</p></li>
<li><p>Processed foods with artificial colors and flavors.</p></li>
</ul>

