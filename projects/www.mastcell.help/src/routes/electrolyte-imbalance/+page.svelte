<p>Electrolyte imbalance names and symptoms:</p>
<ul>
<li><p><strong>Hyponatremia</strong>: A low sodium level can result in symptoms like muscle cramps, nausea, and in severe cases, seizures or coma.</p></li>
<li><p><strong>Hypernatremia</strong>: An elevated sodium level can lead to symptoms like excessive thirst, dry mouth, and confusion.</p></li>
<li><p><strong>Hypokalemia</strong>: Low potassium levels can cause muscle weakness, irregular heart rhythms, and fatigue.</p></li>
<li><p><strong>Hyperkalemia</strong>: High potassium levels can lead to muscle weakness, cardiac arrhythmias, and in severe cases, cardiac arrest.</p></li>
<li><p><strong>Hypocalcemia</strong>: Low calcium levels can result in muscle spasms, numbness, and tingling in the extremities.</p></li>
<li><p><strong>Hypercalcemia</strong>: Elevated calcium levels can lead to symptoms like fatigue, constipation, and confusion.</p></li>
</ul>

