<h1 id="trigger-triage-kitchen">Trigger triage kitchen</h1>
<p>Trigger triage kitchen advice from <a href="https://SurvivingToxicMold.com">SurvivingToxicMold.com</a>.</p>
<p><strong>Groceries</strong>:</p>
<ul>
<li><p>Foods: No gluten, dairy, yeast, legumes. No processed foods. No moldy foods. No artificial additives.</p></li>
<li><p>Drinks: Lots of filtered water. No alcohol, coffee, caffeine.</p></li>
<li><p>Supplements: Natural antifungals, binders, vitamins, minerals, herbals, electrolytes.</p></li>
<li><p>Personal care: Replace all toxic products with hypoallergenic products. No fluoride. No perfumes.</p></li>
</ul>
<p><strong>Kitchen</strong>:</p>
<ul>
<li><p>Cookware: No teflon. No aluminum. Prefer stainless steel pans.</p></li>
<li><p>Containers: No plastic. Prefer glass or stainless steel.</p></li>
<li><p>Wipes: Switch from paper to all-natural organic cloth.</p></li>
</ul>

