<h1 id="allergic-conjunctivitis">Allergic conjunctivitis</h1>
<p>Allergic conjunctivitis is a common condition characterized by inflammation of the conjunctiva, the thin membrane that covers the white part of the eye and the inner surface of the eyelids. It occurs when the eyes come into contact with allergens such as pollen, dust mites, pet dander, or certain chemicals, triggering an allergic reaction.</p>
<p><strong>Key points</strong>:</p>
<ul>
<li><p><strong>Types</strong>: Seasonal allergic conjunctivitis (SAC) is usually in response to outdoor allergens such as pollen. Perennial allergic conjunctivitis (PAC) occurs year-round and is typically caused by indoor allergens such as mites, dander, mold, or chemicals. Vernal keratoconjunctivitis (VKC) and atopic keratoconjunctivitis (AKC) are less common but more severe forms.</p></li>
<li><p><strong>Treatment</strong>: Avoidance of allergens, topical <a href="/antihistamines/">antihistamines</a>, topical <a href="/mast-cell-stabilizers/">mast cell stabilizers</a>, <a href="/corticosteroids/">corticosteroids</a>, and cold compresses.</p></li>
</ul>

