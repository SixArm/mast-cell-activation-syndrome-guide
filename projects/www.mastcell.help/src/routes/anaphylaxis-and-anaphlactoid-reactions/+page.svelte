<h1 id="anaphylaxis-and-anaphlactoid">Anaphylaxis and anaphlactoid</h1>
<p>Anaphylaxis and anaphlactoid are similar words for similar symptoms. The difference is whether there’s an allergic reaction or not.</p>
<p>In an allergic reaction, there is an identifiable antigen.</p>
<ul>
<li><p>The body’s immune system recognizes the antigen, and binds it with an antibody called immunoglobin E (IgE).</p></li>
<li><p>The result is called an “antibody-antigen complex”, and it triggers receptors on mast cells and basophils.</p></li>
<li><p>This causes the release of histamine and other chemicals into the body, and generates the hypersensitivity response.</p></li>
<li><p>If the reaction is localized, then it is stereotyped as an allergic reaction.</p></li>
<li><p>If the reaction is systemic, then it is stereotyped as an anaphylactic reaction a.ka. anaphylaxis.</p></li>
</ul>
<p>In contrast, an anaphylactoid reaction occurs without an identifiable antigen.</p>
<ul>
<li><p>An anaphylactoid reaction does generate mast cell (or basophil) degranulation.</p></li>
<li><p>And does have many of the same signs, symptoms, and responses, as anaphylaxis.</p></li>
<li><p>But does not create an “antibody-antigen complex” so cannot be detected by many allergy tests.</p></li>
<li><p>Examples are MCAS reactions to sunshine, or heat, or vibration, or foods that don’t cause IgE binding.</p></li>
</ul>
<p>Many adverse reactions to drugs, or drug hypersensitivies, are actually anaphylactoid reactions and not true allergies.</p>

