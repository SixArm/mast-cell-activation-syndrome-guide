<h1 id="anaphylaxis-diagnosis">Anaphylaxis diagnosis</h1>
<p>Diagnosing anaphylaxis is primarily based on clinical evaluation and
a thorough medical history, as well as the recognition of characteristic
signs and symptoms that occur after exposure to an allergen or
trigger.</p>
<p>Key points:</p>
<ul>
<li><p>Medical History: A provider will take a detailed medical history,
which includes asking about the individual’s symptoms, the timing of
symptom onset in relation to exposure to a potential allergen or
trigger, and any known allergies or previous episodes of
anaphylaxis.</p></li>
<li><p>Physical Examination: A provider will assess the individual’s
vital signs, including pulse rate, blood pressure, and respiratory rate.
Particular attention is given to evaluating the presence of physical
signs such as hives, swelling (especially of the face, lips, or throat),
and any respiratory distress.</p></li>
<li><p>Elimination of Other Causes: A provider will consider other
potential causes for the symptoms and rule out alternative diagnoses.
This includes differentiating anaphylaxis from other medical conditions
that may mimic its symptoms, such as severe asthma exacerbations or
cardiovascular events.</p></li>
<li><p>Timing and Trigger: A provider will assess whether the onset of
symptoms occurred shortly after exposure to a known allergen or trigger,
which can help confirm the diagnosis of anaphylaxis.</p></li>
<li><p>Laboratory Tests: While laboratory tests are not typically used
for diagnosing anaphylaxis, certain tests may be performed to help
confirm the diagnosis and identify potential triggers. These may include
serum tryptase levels, which can be elevated in the bloodstream during
anaphylaxis.</p></li>
</ul>

