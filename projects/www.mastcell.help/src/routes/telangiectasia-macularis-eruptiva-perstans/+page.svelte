<h1 id="telangiectasia-macularis-eruptiva-perstans-tmep">Telangiectasia Macularis Eruptiva Perstans (TMEP)</h1>
<p>Telangiectasia Macularis Eruptiva Perstans (TMEP) is a rare skin disorder characterized by the presence of small, red to violet, macular (flat) skin lesions known as telangiectasias. These are caused by the dilation of small blood vessels near the surface of the skin. TMEP is a subtype of cutaneous mastocytosis, a group of disorders associated with an abnormal accumulation of mast cells in the skin.</p>
<p><strong>Key aspects:</strong></p>
<ul>
<li><p><strong>Skin Lesions:</strong> The primary feature is the presence of telangiectasias, which are small, red to violet spots on the skin. These spots are typically flat and may vary in size.</p></li>
<li><p><strong>Mast Cell Infiltration:</strong> Mast cells are a type of immune cell that plays a role in allergic reactions. In TMEP, mast cells accumulate in the skin.</p></li>
<li><p><strong>Itching and Flushing:</strong> Many individuals with TMEP experience itching (pruritus) and may also have episodes of skin flushing, which can be triggered by heat, exercise, and stress.</p></li>
<li><p><strong>Systemic Symptoms:</strong> Unlike systemic mastocytosis, TMEP primarily involves the skin. Symptoms like abdominal pain, diarrhea, or anaphylaxis are typically absent.</p></li>
<li><p><strong>Rare Condition:</strong> TMEP is a rare condition, and its cause is not fully understood. It is thought to be associated with genetic mutations that lead to the abnormal accumulation of mast cells in the skin.</p></li>
<li><p><strong>Diagnosis:</strong> May include a skin biopsy and specific tests to detect mast cell disorders.</p></li>
<li><p><strong>Treatment:</strong> Focus on controlling symptoms, including the use of antihistamines to reduce itching, and avoiding triggers that may cause flushing.</p></li>
</ul>

