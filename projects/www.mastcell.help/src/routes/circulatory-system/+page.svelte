<h1 id="circulatory-system">Circulatory system</h1>
<p>The circulatory system, also known as the cardiovascular system, is a vital organ system in the human body responsible for the transportation of blood, oxygen, nutrients, hormones, and waste products to and from cells throughout the body.</p>
<p><strong>Key components</strong>:</p>
<ul>
<li><p><strong>Heart</strong>: The heart is a muscular organ that acts as the pump of the circulatory system. It contracts rhythmically to propel blood throughout the body. It is divided into four chambers: the left and right atria, which receive blood, and the left and right ventricles, which pump blood out of the heart.</p></li>
<li><p><strong>Blood and blood vessels</strong>: Blood is a fluid connective tissue that flows through the circulatory system via blood vessels. It consists of various components, including red blood cells (erythrocytes) that carry oxygen, white blood cells (leukocytes) that fight infection, platelets that help in clotting, and plasma that contains water, electrolytes, and proteins.</p></li>
</ul>
<p>Key functions:</p>
<ul>
<li><p><strong>Transportation</strong>: Transport oxygen from the lungs to body tissues. Transport carbon dioxide from tissues to the lungs for elimination. Carry nutrients from the digestive system to cells. Remove metabolic waste products.</p></li>
<li><p><strong>Immune Response</strong>: White blood cellsh elp protect the body against infections and foreign invaders.</p></li>
<li><p><strong>Hormone Distribution</strong>: Hormones produced by endocrine glands are released into the bloodstream and distributed throughout the body.</p></li>
<li></li>
<li><p><strong>Temperature Regulation</strong>: Blood helps distribute heat throughout the body, maintaining a stable core body temperature.</p></li>
<li><p><strong>Clotting</strong>: Platelets and clotting factors in the blood are essential for stopping bleeding when blood vessels are damaged.</p></li>
<li><p><strong>Regulation of Blood Pressure</strong>: The circulatory system helps regulate blood pressure, ensuring that blood flows at an appropriate pressure to reach all parts of the body.</p></li>
</ul>

