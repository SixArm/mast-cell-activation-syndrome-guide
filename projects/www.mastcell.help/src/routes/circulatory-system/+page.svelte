<h1 id="circulatory-system">Circulatory system</h1>
<p>The circulatory system, also known as the cardiovascular system, is a
vital organ system in the human body responsible for the transportation
of blood, oxygen, nutrients, hormones, and waste products to and from
cells throughout the body. It plays a central role in maintaining
homeostasis, which is the body’s ability to maintain a stable and
balanced internal environment. The circulatory system consists of the
heart, blood vessels, and blood.</p>
<p>Key components…</p>
<ul>
<li><p>Heart: The heart is a muscular organ that acts as the pump of the
circulatory system. It contracts rhythmically to propel blood throughout
the body. It is divided into four chambers: the left and right atria,
which receive blood, and the left and right ventricles, which pump blood
out of the heart.</p></li>
<li><p>Blood Vessels: Blood vessels are a network of tubular structures
that carry blood. There are three main types of blood vessels: arteries,
veins, and capillaries. Arteries carry oxygenated blood away from the
heart to various parts of the body. Veins transport deoxygenated blood
from body tissues back to the heart. Capillaries are tiny, thin-walled
vessels that connect arteries and veins. They facilitate the exchange of
oxygen, nutrients, and waste products between blood and cells.</p></li>
<li><p>Blood: Blood is a fluid connective tissue that flows through the
circulatory system. It consists of various components, including red
blood cells (erythrocytes) that carry oxygen, white blood cells
(leukocytes) that fight infection, platelets that help in clotting, and
plasma, which is a liquid component containing water, electrolytes, and
proteins.</p></li>
</ul>

