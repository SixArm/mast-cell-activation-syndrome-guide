<h1 id="mold-exposure-diagnostics">Mold exposure diagnostics</h1>
<p>Mold exposure diagnostics involve a combination of clinical assessment, medical history, and various diagnostic tests to determine if an individual has been exposed to mold and if the exposure is contributing to health issues.</p>
<p><strong>Common approaches</strong>:</p>
<ul>
<li><p><strong>Environmental History</strong>: Gathering information about the individualâ€™s living and working environments is essential. Questions may be asked about the presence of water damage, visible mold, or other conditions that could contribute to mold growth.</p></li>
<li><p><strong>Mold Testing</strong>: This may involve air sampling, surface sampling, or bulk sampling to identify the presence and types of molds.</p></li>
<li><p><strong>Serum IgE Testing</strong>: Blood tests may be performed to measure specific antibodies, such as immunoglobulin E (IgE), that the body produces in response to mold allergens.</p></li>
<li><p><strong>Skin Prick Testing</strong>: Skin prick testing involves applying small amounts of mold extracts to the skin and observing for allergic reactions.</p></li>
<li><p><strong>Imaging Studies</strong>: In some cases, imaging studies such as chest X-rays or CT scans may be performed to assess respiratory symptoms and identify any potential lung issues.</p></li>
<li><p><strong>Clinical Laboratory Tests</strong>: Blood tests or other laboratory tests may be ordered to assess general health, including markers of inflammation or immune system function.</p></li>
</ul>

