<h1 id="histamine-metabolism-of-foods">Histamine metabolism of foods</h1>
<p><a href="https://www.histaminintoleranz.ch/downloads/SIGHI-Leaflet_HistamineEliminationDiet.pdf">Source</a></p>
<p>Foods affect the histamine levels in a variety of ways.</p>
<p><strong>Mechanisms</strong>:</p>
<ul>
<li><p><strong>Histamine-containing foods</strong>: Histamine is formed as a deterioration product
in perishable food, in microbial fermentation and maturation processes and in
the ripening of fruit. Medication: DAOSIN (works best if ingested
15-30 minutes before meal), H1 antihistamines, possibly H2 antihistamines.</p></li>
<li><p><strong>Biogenic amines besides histamine</strong>: Several amines share the same main
degradation pathway with histamine: They are competing substrates, degraded by
the same enzyme diamine oxidase (DAO). The DAO prefers other amines before it
degrades histamine. Medication: H1 antihistamines, DAOSIN.</p></li>
<li><p><strong>Histamine liberators</strong>: Some foods and additives release endogenous
histamine from certain cell types (mainly from mast cells). This mechanism is
independent from a lack of diamine oxidase (DAO). Medication: H1
antihistamines, cromoglicic acid (or its salt sodium cromoglicate or cromolyn
sodium), ibuprofen. DAOSIN has no direct effect.</p></li>
<li><p><strong>Diamine oxidase inhibitors</strong>: Diamine oxidase is a sensitive molecule that
can be inhibited in its activity by chemical influences. Foods and additives
can block the breakdown of histamine by the DAO. Medication: H1
antihistamines. DAOSIN supports the reduction of biogenic amines, but is
ineffective against DAO inhibitors.</p></li>
<li><p><strong>Intestinal permeability</strong>: Certain substances increase the intestinal
permeability (“Leaky Gut Syndrome”) and risk to develop an IgE or IgG food
allergy or poisoning. Medication: mast cell stabilizers with continuous
application, H1 antihistamines. DAOSIN has no effect.</p></li>
</ul>

