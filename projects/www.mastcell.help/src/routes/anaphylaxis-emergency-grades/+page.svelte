<h1 id="anaphylaxis-emergency-grades">Anaphylaxis emergency grades</h1>
<p><a href="/anaphylaxis/">Anaphylaxis</a> emergency grades for communicating with emergency healthcare providers:</p>
<ul>
<li><p><strong>Grade 1</strong>: hives, rash, itching, swelling of mouth/throat.</p></li>
<li><p><strong>Grade 2</strong>: any above plus hypotension, tachycardia, dyspnea, presyncope, GI distress (including but not limited to pain, nausea, vomiting, diarrhea)</p></li>
<li><p><strong>Grade 3</strong>: any of the above, plus profound hypotension, bradycardia or tachycardia, confusion, cardiovascular collapse, bronchospasm, hypoxia (SaO2 &lt; 92%).</p></li>
<li><p><strong>Grade 4</strong>: cardiac arrest</p></li>
</ul>

