<h1 id="anaphylaxis-emergency-grades">Anaphylaxis emergency grades</h1>
<p>Anaphylaxis emergency grades for communicating with emergency
healthcare providers:</p>
<ul>
<li><p>Grade 1: hives, rash, itching, swelling of mouth/throat.</p></li>
<li><p>Grade 2: any above plus hypotension, tachycardia, dyspnea,
presyncope, GI distress (including but not limited to pain, nausea,
vomiting, diarrhea)</p></li>
<li><p>Grade 3: any of the above, plus profound hypotension, bradycardia
or tachycardia, confusion, cardiovascular collapse, bronchiospasm,
hypoxia (SaO2 &lt; 92%).</p></li>
<li><p>Grade 4: cardiac arrest</p></li>
</ul>

