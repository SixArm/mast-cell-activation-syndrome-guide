<h1 id="antihistamine-medications">Antihistamine medications</h1>
<p>Antihistamine medications are a class of drugs commonly used to relieve allergy symptoms and manage various allergic conditions. They work by blocking the effects of histamine, a chemical released by the body in response to allergens.</p>
<p>First-generation antihistamines have been in use for many years and are available over-the-counter and by prescription. They are effective but often cause drowsiness and may impair cognitive and motor skills.</p>
<ul>
<li>Diphenhydramine (Benadryl)</li>
<li>Chlorpheniramine (Chlor-Trimeton)</li>
<li>Clemastine (Tavist)</li>
<li>Promethazine (Phenergan)</li>
</ul>
<p>Second-generation antihistamines are newer and have been developed to reduce drowsiness and other side effects commonly associated with first-generation antihistamines.</p>
<ul>
<li>Loratadine (Claritin)</li>
<li>Cetirizine (Zyrtec)</li>
<li>Fexofenadine (Allegra)</li>
<li>Desloratadine (Clarinex)</li>
<li>Ketotifen (Zaditor)</li>
</ul>
<p>Third-generation antihistamines are similar to second-generation antihistamines in terms of reduced sedation but may have a faster onset of action and longer duration of effect.</p>
<ul>
<li>Levocetirizine (Xyzal)</li>
<li>Desloratadine (Clarinex)</li>
<li>Bilastine (Blexten)</li>
</ul>
<p>Prescription antihistamines are used to treat severe allergies or chronic conditions.</p>
<ul>
<li>Azelastine (Astelin)</li>
<li>Olopatadine (Patanase)</li>
<li>Cyproheptadine (Periactin)</li>
</ul>

