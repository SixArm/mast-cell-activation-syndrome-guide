<h1 id="chronic-fatigue-syndrome-cfs">Chronic Fatigue Syndrome (CFS)</h1>
<p>Chronic Fatigue Syndrome (CFS), also known as myalgic encephalomyelitis (ME), is a complex and debilitating medical condition characterized by persistent and unexplained fatigue that is not alleviated by rest. CFS is a chronic condition.</p>
<p><strong>Key points</strong>:</p>
<ul>
<li><p><strong>Fatigue and Exhaustion</strong>: The hallmark symptom is severe fatigue that is not relieved by rest. This is often accompanied by a disabling decrease in energy.</p></li>
<li><p><strong>Additional Symptoms</strong>: Cognitive difficulties, unrefreshing sleep, muscle and joint pain, headaches, sore throat, and tender lymph nodes.</p></li>
<li><p><strong>Post-Exertional Malaise (PEM)</strong>: CFS is often associated with post-exertional malaise, where physical or mental exertion can exacerbate symptoms and lead to a prolonged worsening of fatigue.</p></li>
<li><p><strong>Diagnosis</strong>: Diagnosing CFS can be challenging as there are no specific tests. Healthcare professionals typically rely on a thorough medical history, physical examination, and the exclusion of other possible causes of fatigue.</p></li>
<li><p><strong>Unknown Cause</strong>: CFS is complex condition with multiple potential contributing factors, including viral infections, immune system dysfunction, hormonal imbalances, and genetic predisposition.</p></li>
<li><p><strong>Multidisciplinary Approach</strong>: Management may include symptom management, cognitive-behavioral therapy (CBT), graded exercise therapy (GET), and lifestyle adjustments.</p></li>
<li><p><strong>Supportive Care</strong>: May include managing sleep disturbances, addressing pain and other symptoms, and providing emotional support.</p></li>
</ul>

