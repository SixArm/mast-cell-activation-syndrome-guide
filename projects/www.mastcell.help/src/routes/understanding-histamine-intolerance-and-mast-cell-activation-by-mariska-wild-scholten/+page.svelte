<p>Understanding Histamine Intolerance &amp; Mast Cell Activation</p>
<p>By Mariska Wild-Scholten (Author)</p>
<p><a href="https://www.amazon.com/Understanding-Histamine-Intolerance-Mast-Activation-ebook/dp/B00YU07VC6">Amazon</a></p>
<p>Histamine intolerance and Mast Cell Activation result in allergy-like symptoms. Histamine-rich food or mast cell degranulators may cause diarrhea or constipation, low or high blood pressure, eczema, asthma, acid reflux, migraine, depression, rapid heart beats and breathing, panic attacks and sleep disturbances.</p>
<p>This book describes the symptoms, assists in diagnosis and treatment. Basic knowledge is given to help patients to understand their enemy. Lists are included with concentrations of biogenic amines, mast cell degranulators and medicines which must be avoided. References and abstracts to scientific literature are provided as well.</p>

