<h1 id="allergy-testing">Allergy testing</h1>
<p>Allergy testing is a medical procedure used to identify specific substances or allergens that may trigger an allergic reaction in an individual.</p>
<p><strong>Common methods:</strong></p>
<ul>
<li><p><strong>Skin Prick Test (SPT):</strong> Apply a small amount of an allergen to the skin, usually on the forearm or back. Prick the skin with a needle to introduce the allergen beneath the skinâ€™s surface. If a person is allergic, they develop a small raised bump or hive.</p></li>
<li><p><strong>Intradermal Test:</strong> Inject a small amount of allergen solution just beneath the surface of the skin. This test is often used when skin prick tests produce inconclusive results.</p></li>
<li><p><strong>Patch Testing:</strong> Apply small amounts of allergens to patches. Place them on the skin, usually the back. The patches are worn for a specific period, and the skin is observed for any reactions.</p></li>
<li><p><strong>Blood Tests:</strong> Blood tests, such as the specific IgE blood test, measure the levels of antibodies (immunoglobulin E or IgE) produced in response to specific allergens. This test is often used when skin testing is not preferred.</p></li>
<li><p><strong>Elimination Diet:</strong> Remove certain foods from the diet, then reintroducing them one at a time to observe any allergic reactions.</p></li>
<li><p><strong>Provocation Testing:</strong> Expose the patient to suspected allergens in a controlled environment, such as inhaling allergens or consuming them under medical supervision.</p></li>
</ul>

