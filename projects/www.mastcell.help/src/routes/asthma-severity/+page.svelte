<h1 id="asthma-severity">Asthma severity</h1>
<p>Asthma is a chronic respiratory condition that can vary in severity
from person to person. Healthcare providers often assess asthma severity
to guide treatment and management strategies.</p>
<p>Intermittent Asthma:</p>
<ul>
<li><p>Attacks are brief and not severe.</p></li>
<li><p>Symptoms occur on two days or fewer per week.</p></li>
<li><p>Asthma night awakenings are two times or fewer per
month.</p></li>
<li><p>Lung function is generally normal between exacerbations.</p></li>
</ul>
<p>Mild Persistent Asthma:</p>
<ul>
<li><p>Attacks affect daily activities but are generally not
severe.</p></li>
<li><p>Symptoms occur more than two days per week but not
daily.</p></li>
<li><p>Asthma night awakenings are more than two times per
month.</p></li>
<li><p>Lung function is generally normal between attacks.</p></li>
</ul>
<p>Moderate Persistent Asthma:</p>
<ul>
<li><p>Attacks affect daily activities and may be severe.</p></li>
<li><p>Daily symptoms are common.</p></li>
<li><p>Asthma night awakenings are more than once a week.</p></li>
<li><p>Lung function is reduced, with FEV1 (forced expiratory volume in
one second) between 60% and 80% of the predicted value.</p></li>
</ul>
<p>Severe Persistent Asthma:</p>
<ul>
<li><p>Attacks limit daily activities, are frequent, and
severe.</p></li>
<li><p>Symptoms occur throughout the day and frequently at
night.</p></li>
<li><p>Asthma night awakenings occur often.</p></li>
<li><p>Lung function is significantly reduced, with FEV1 less than 60%
of the predicted value.</p></li>
</ul>

