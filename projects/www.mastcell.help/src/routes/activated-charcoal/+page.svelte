<h1 id="activated-charcoal">Activated charcoal</h1>
<p>Activated charcoal, also known as activated carbon, is a form of
carbon that has been processed to have a large surface area, making it
highly adsorbent. It is commonly used for its ability to adsorb (bind
to) a wide range of substances, including toxins, chemicals, and
impurities.</p>
<h3 id="medical-use">Medical Use</h3>
<p>Activated charcoal is used in medical settings to treat certain types
of poisoning or overdose. It works by binding to the toxic substances in
the gastrointestinal tract, preventing their absorption into the
bloodstream and facilitating their elimination from the body. This can
be useful in cases of poisoning by drugs, chemicals, or other toxic
substances.</p>
<p>While activated charcoal can be effective for certain types of
poisoning, it may not be suitable for all situations. In some cases, it
can be contraindicated, and other treatments may be more appropriate.
Activated charcoal should only be administered under the guidance of
healthcare professionals to ensure its safe and appropriate use.</p>
<h3 id="over-the-counter-products">Over-the-Counter Products</h3>
<p>Activated charcoal is available over the counter in various forms,
such as capsules and powder. Some people use it as a dietary supplement
or for digestive issues, claiming that it can help alleviate gas,
bloating, and indigestion.</p>
<p>When taken internally, it can also lead to potential side effects,
including constipation, black stools, and gastrointestinal discomfort.
It can also interfere with the absorption of medications and nutrients
when taken internally. Therefore, it should not be used as a supplement
without the guidance of a healthcare provider.</p>
<h3 id="teeth-whitening">Teeth Whitening</h3>
<p>Activated charcoal is sometimes used in dental products, such as
toothpaste and mouthwash, as a potential teeth-whitening agent. However,
its efficacy for this purpose is a subject of debate, and its abrasive
nature can potentially harm tooth enamel.</p>
